        code.AppendLine("// 警告: 请勿手动修改此文件，所有更改都会在下次生成时被覆盖");
        code.AppendLine();
        code.AppendLine("#nullable enable");
        code.AppendLine("#pragma warning disable CS1591 // 缺少对公共可见类型或成员的 XML 注释");
        code.AppendLine();
        code.AppendLine("using System;");
        code.AppendLine("using System.Collections.Generic;");
        code.AppendLine("using System.Collections.Concurrent;");
        code.AppendLine("using System.Threading;");
        code.AppendLine("using System.Threading.Tasks;");
        code.AppendLine("using System.Runtime.CompilerServices;");
        code.AppendLine("using System.Diagnostics;");
        code.AppendLine("using Microsoft.Extensions.Logging;");
        code.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        code.AppendLine();
    }
    
