    /// <summary>
    /// 生成消息类型映射
    /// </summary>
    private static void GenerateMessageTypeMapping(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 预编译的消息类型映射 - 避免运行时Type.GetType调用");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    private static readonly Dictionary<string, Type> MessageTypeMap = new(StringComparer.Ordinal)");
        code.AppendLine("    {");
        
        var allMessageTypes = new HashSet<string>();
        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                // 参数类型作为请求类型
                if (method.IsSingleParameter && !allMessageTypes.Contains(method.FirstParameter!.TypeFullName))
                {
                    allMessageTypes.Add(method.FirstParameter!.TypeFullName);
                    code.AppendLine($"        [\"{method.FirstParameter!.TypeFullName}\"] = typeof({method.FirstParameter!.TypeFullName}),");
                }
                
                // 返回类型作为响应类型
                if (!string.IsNullOrEmpty(method.ReturnTypeFullName) && method.ReturnTypeFullName != "void" && 
                    method.ReturnTypeFullName != "System.Threading.Tasks.Task" && 
                    !allMessageTypes.Contains(method.ReturnTypeFullName))
                {
                    allMessageTypes.Add(method.ReturnTypeFullName);
                    code.AppendLine($"        [\"{method.ReturnTypeFullName}\"] = typeof({method.ReturnTypeFullName}),");
                }
            }
        }
        
        code.AppendLine("    };");
        code.AppendLine();
    }
    
    /// <summary>
    /// 生成处理器映射
    /// </summary>
    private static void GenerateHandlerMapping(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 预编译的消息处理器映射 - 直接方法调用，无反射开销");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    private static readonly Dictionary<Type, MessageHandlerInfo> HandlerMap = new()");
        code.AppendLine("    {");
        
        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                if (method.IsSingleParameter)
                {
                    var handlerName = $"Handle{service.InterfaceName.TrimStart('I')}{method.MethodName}";
                    var isAsync = method.IsAsync;
                    var serviceName = service.InterfaceName;
                    
                    code.AppendLine($"        [typeof({method.FirstParameter!.TypeFullName})] = new MessageHandlerInfo");
                    code.AppendLine("        {");
                    code.AppendLine($"            ServiceType = typeof({serviceName}),");
                    code.AppendLine($"            MethodName = \"{method.MethodName}\",");
                    code.AppendLine($"            IsAsync = {isAsync.ToString().ToLower()},");
                    code.AppendLine($"            Handler = {handlerName},");
                    code.AppendLine($"            Priority = MessagePriority.{GetMessagePriority(method)},");
                    code.AppendLine($"            EstimatedLatencyMs = {GetEstimatedLatency(method)},");
                    code.AppendLine("        },");
                }
            }
        }
        
        code.AppendLine("    };");
        code.AppendLine();
    }
    
    /// <summary>
    /// 生成快速分发方法
    /// </summary>
    private static void GenerateDispatchMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 高性能消息分发 - 编译时优化的快速路径");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        code.AppendLine("    public static async ValueTask<object?> DispatchAsync(");
        code.AppendLine("        object message, ");
        code.AppendLine("        IServiceProvider serviceProvider, ");
        code.AppendLine("        CancellationToken cancellationToken = default)");
        code.AppendLine("    {");
        code.AppendLine("        var messageType = message.GetType();");
        code.AppendLine("        ");
        code.AppendLine("        // 快速路径：预编译的类型检查和分发");
        code.AppendLine("        if (HandlerMap.TryGetValue(messageType, out var handlerInfo))");
        code.AppendLine("        {");
        code.AppendLine("            var startTime = Stopwatch.GetTimestamp();");
        code.AppendLine("            ");
        code.AppendLine("            try");
        code.AppendLine("            {");
        code.AppendLine("                var result = await handlerInfo.Handler(message, serviceProvider, cancellationToken);");
        code.AppendLine("                ");
        code.AppendLine("                // 记录成功指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchSuccess(messageType.Name, elapsedMs);");
        code.AppendLine("                ");
        code.AppendLine("                return result;");
        code.AppendLine("            }");
        code.AppendLine("            catch (Exception ex)");
        code.AppendLine("            {");
        code.AppendLine("                // 记录错误指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchError(messageType.Name, elapsedMs, ex);");
        code.AppendLine("                throw;");
        code.AppendLine("            }");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        // 慢速路径：未识别的消息类型");
        code.AppendLine("        Metrics.RecordUnknownMessageType(messageType.Name);");
        code.AppendLine("        throw new InvalidOperationException($\"未找到消息类型 '{messageType.Name}' 的处理器\");");
        code.AppendLine("    }");
        code.AppendLine();
    }
    
    /// <summary>
    /// 生成批量分发方法
    /// </summary>
    private static void GenerateBatchDispatchMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 批量消息分发 - 优化的批处理路径");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public static async ValueTask<object?[]> DispatchBatchAsync(");
        code.AppendLine("        ReadOnlyMemory<object> messages, ");
        code.AppendLine("        IServiceProvider serviceProvider, ");
        code.AppendLine("        CancellationToken cancellationToken = default)");
        code.AppendLine("    {");
        code.AppendLine("        var results = new object?[messages.Length];");
        code.AppendLine("        var tasks = new ValueTask<object?>[messages.Length];");
        code.AppendLine("        ");
        code.AppendLine("        // 并行分发所有消息");
        code.AppendLine("        var messageSpan = messages.Span;");
        code.AppendLine("        for (int i = 0; i < messageSpan.Length; i++)");
        code.AppendLine("        {");
        code.AppendLine("            tasks[i] = DispatchAsync(messageSpan[i], serviceProvider, cancellationToken);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        // 等待所有任务完成");
        code.AppendLine("        for (int i = 0; i < tasks.Length; i++)");
        code.AppendLine("        {");
        code.AppendLine("            results[i] = await tasks[i];");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        return results;");
        code.AppendLine("    }");
        code.AppendLine();
    }
    
    /// <summary>
    /// 生成具体的处理器方法
    /// </summary>
    private static void GenerateHandlerMethods(StringBuilder code, List<ServiceModel> serviceModels)
    {
        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                if (method.IsSingleParameter)
                {
                    var handlerName = $"Handle{service.InterfaceName.TrimStart('I')}{method.MethodName}";
                    var serviceName = service.InterfaceName;
                    var requestType = method.FirstParameter!.TypeFullName;
                    var responseType = method.ReturnTypeFullName ?? "object";
                    var isAsync = method.IsAsync;
                    
                    code.AppendLine($"    /// <summary>");
                    code.AppendLine($"    /// 生成的处理器：{serviceName}.{method.MethodName}");
                    code.AppendLine($"    /// </summary>");
                    code.AppendLine($"    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
                    code.AppendLine($"    private static async ValueTask<object?> {handlerName}(");
                    code.AppendLine($"        object message, ");
                    code.AppendLine($"        IServiceProvider serviceProvider, ");
                    code.AppendLine($"        CancellationToken cancellationToken)");
                    code.AppendLine($"    {{");
                    code.AppendLine($"        var typedMessage = ({requestType})message;");
                    code.AppendLine($"        var service = serviceProvider.GetRequiredService<{serviceName}>();");
                    code.AppendLine($"        ");
                    
                    if (isAsync)
                    {
                        if (responseType == "void" || responseType == "System.Threading.Tasks.Task")
                        {
                            code.AppendLine($"        await service.{method.MethodName}(typedMessage, cancellationToken).ConfigureAwait(false);");
                            code.AppendLine($"        return null;");
                        }
                        else
                        {
                            code.AppendLine($"        var result = await service.{method.MethodName}(typedMessage, cancellationToken).ConfigureAwait(false);");
                            code.AppendLine($"        return result;");
                        }
                    }
                    else
                    {
                        if (responseType == "void")
                        {
                            code.AppendLine($"        service.{method.MethodName}(typedMessage);");
                            code.AppendLine($"        return null;");
                        }
                        else
                        {
                            code.AppendLine($"        var result = service.{method.MethodName}(typedMessage);");
                            code.AppendLine($"        return result;");
                        }
                    }
                    
                    code.AppendLine($"    }}");
                    code.AppendLine();
                }
            }
        }
    }
    
    /// <summary>
    /// 生成统计和监控方法
    /// </summary>
    private static void GenerateStatisticsMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 获取分发器统计信息");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public static DispatcherStatistics GetStatistics()");
        code.AppendLine("    {");
        code.AppendLine("        return new DispatcherStatistics");
        code.AppendLine("        {");
        code.AppendLine($"            TotalHandlers = {GetTotalHandlerCount(serviceModels)},");
        code.AppendLine($"            TotalServices = {serviceModels.Count},");
        code.AppendLine("            Metrics = Metrics.GetSnapshot(),");
        code.AppendLine($"            GeneratedAt = DateTime.UtcNow,");
        code.AppendLine("            HandlerMap = HandlerMap.ToDictionary(");
        code.AppendLine("                kvp => kvp.Key.Name, ");
        code.AppendLine("                kvp => kvp.Value)");
        code.AppendLine("        };");
        code.AppendLine("    }");
        code.AppendLine();
        
        // 生成性能指标类
        GenerateMetricsClass(code);
        
        // 生成统计数据类
        GenerateStatisticsDataClasses(code);
        
        // 生成具体的处理器方法
        GenerateHandlerMethods(code, serviceModels);
    }
    
    /// <summary>
    /// 生成性能指标类
    /// </summary>
    private static void GenerateMetricsClass(StringBuilder code)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 分发器性能指标");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public static class Metrics");
        code.AppendLine("    {");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> SuccessCount = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> ErrorCount = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, double> TotalLatency = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> UnknownTypes = new();");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordDispatchSuccess(string messageType, double latencyMs)");
        code.AppendLine("        {");
        code.AppendLine("            SuccessCount.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("            TotalLatency.AddOrUpdate(messageType, latencyMs, (_, total) => total + latencyMs);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordDispatchError(string messageType, double latencyMs, Exception ex)");
        code.AppendLine("        {");
        code.AppendLine("            ErrorCount.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("            TotalLatency.AddOrUpdate(messageType, latencyMs, (_, total) => total + latencyMs);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordUnknownMessageType(string messageType)");
        code.AppendLine("        {");
        code.AppendLine("            UnknownTypes.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static MetricsSnapshot GetSnapshot()");
        code.AppendLine("        {");
        code.AppendLine("            return new MetricsSnapshot");
        code.AppendLine("            {");
        code.AppendLine("                SuccessCount = new Dictionary<string, long>(SuccessCount),");
        code.AppendLine("                ErrorCount = new Dictionary<string, long>(ErrorCount),");
        code.AppendLine("                TotalLatency = new Dictionary<string, double>(TotalLatency),");
        code.AppendLine("                UnknownTypes = new Dictionary<string, long>(UnknownTypes)");
        code.AppendLine("            };");
        code.AppendLine("        }");
        code.AppendLine("    }");
        code.AppendLine();
    }
    
