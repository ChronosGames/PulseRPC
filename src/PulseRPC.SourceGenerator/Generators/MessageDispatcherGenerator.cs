using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using PulseRPC.SourceGenerator.Models;

namespace PulseRPC.SourceGenerator.Generators;

/// <summary>
/// 编译时消息分发器生成器 - 消除运行时反射开销
/// </summary>
public static class MessageDispatcherGenerator
{
    /// <summary>
    /// 生成编译时消息分发器代码
    /// </summary>
    public static string GenerateSourceText(List<ServiceModel> serviceModels)
    {
        var code = new StringBuilder();
        
        // 生成文件头
        GenerateFileHeader(code);
        
        // 生成命名空间和类开始
        GenerateClassHeader(code);
        
        // 生成消息类型映射
        GenerateMessageTypeMapping(code, serviceModels);
        
        // 生成处理器映射
        GenerateHandlerMapping(code, serviceModels);
        
        // 生成快速分发方法
        GenerateDispatchMethod(code, serviceModels);
        
        // 生成批量分发方法
        GenerateBatchDispatchMethod(code, serviceModels);
        
        // 生成统计和监控方法
        GenerateStatisticsMethod(code, serviceModels);
        
        // 生成类结尾
        GenerateClassFooter(code);
        
        return code.ToString();
    }
    
    /// <summary>
    /// 生成文件头
    /// </summary>
    private static void GenerateFileHeader(StringBuilder code)
    {
