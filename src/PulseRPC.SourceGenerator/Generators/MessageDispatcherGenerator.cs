    /// <summary>
    /// 生成类结尾
    /// </summary>
    private static void GenerateClassFooter(StringBuilder code)
    {
        code.AppendLine("}");
        code.AppendLine();
        code.AppendLine("#pragma warning restore CS1591");
    }
    
    /// <summary>
    /// 获取消息优先级
    /// </summary>
    private static string GetMessagePriority(MethodModel method)
    {
        // 根据方法特性推断优先级
        if (method.MethodName.Contains("Critical") || method.MethodName.Contains("Emergency"))
            return "Critical";
        if (method.MethodName.Contains("High") || method.MethodName.Contains("Priority"))
            return "High";
        if (method.MethodName.Contains("Low") || method.MethodName.Contains("Background"))
            return "Low";
        return "Normal";
    }
    
    /// <summary>
    /// 获取预估延迟
    /// </summary>
    private static double GetEstimatedLatency(MethodModel method)
    {
        // 根据方法复杂度预估延迟
        if (method.IsAsync)
            return 2.0; // 异步方法通常更快
        if (method.MethodName.Contains("Query") || method.MethodName.Contains("Get"))
            return 1.0; // 查询操作较快
        if (method.MethodName.Contains("Create") || method.MethodName.Contains("Update"))
            return 5.0; // 写入操作较慢
        return 3.0; // 默认延迟
    }
    
    /// <summary>
    /// 获取总处理器数量
    /// </summary>
    private static int GetTotalHandlerCount(List<ServiceModel> serviceModels)
    {
        return serviceModels.Sum(s => s.Methods.Count(m => m.IsSingleParameter));
    }
