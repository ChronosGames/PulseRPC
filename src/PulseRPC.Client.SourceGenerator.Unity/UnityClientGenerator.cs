namespace PulseRPC.Client.SourceGenerator.Unity;

[Generator]
public class ServiceProxyGenerator : ISourceGenerator
{
    public void Initialize(GeneratorInitializationContext context)
    {
        // 注册语法接收器
        context.RegisterForSyntaxNotifications(() => new SyntaxReceiver());
    }

    public void Execute(GeneratorExecutionContext context)
    {
        // 获取语法接收器
        if (!(context.SyntaxContextReceiver is SyntaxReceiver receiver))
            return;

        // 生成每个服务接口的代理
        foreach (var serviceInterface in receiver.CandidateInterfaces)
        {
            string proxySource = GenerateProxy(serviceInterface, context);
            context.AddSource($"{serviceInterface.Name}Proxy.g.cs", SourceText.From(proxySource, Encoding.UTF8));
        }
    }

    private string GenerateProxy(INamedTypeSymbol interfaceSymbol, GeneratorExecutionContext context)
    {
        string namespaceName = interfaceSymbol.ContainingNamespace.ToDisplayString();
        string interfaceName = interfaceSymbol.Name;
        string proxyClassName = $"{interfaceName.Substring(1)}Proxy"; // 去掉I前缀

        StringBuilder source = new StringBuilder();
        source.AppendLine($@"// <auto-generated />
using System;
using System.Threading;
using System.Threading.Tasks;
using System.Collections.Generic;
using GameNetwork.Core;

namespace {namespaceName}
{{
    /// <summary>
    /// 为{interfaceName}自动生成的代理类
    /// </summary>
    public class {proxyClassName} : {interfaceName}
    {{
        private readonly IMessageChannel _messageChannel;

        public {proxyClassName}(IMessageChannel messageChannel)
        {{
            _messageChannel = messageChannel;
        }}
");

        // 为接口中的每个方法生成实现
        foreach (var method in interfaceSymbol.GetMembers().OfType<IMethodSymbol>())
        {
            GenerateMethodImplementation(source, method);
        }

        // 闭合类和命名空间
        source.AppendLine("    }");
        source.AppendLine("}");

        return source.ToString();
    }

    private void GenerateMethodImplementation(StringBuilder source, IMethodSymbol method)
    {
        string methodName = method.Name;
        bool isSubscriptionMethod = methodName.StartsWith("SubscribeTo") ||
                                    method.GetAttributes().Any(a =>
                                        a.AttributeClass.Name == "SubscriptionMethodAttribute");
        bool isClientOnly = method.GetAttributes().Any(a => a.AttributeClass.Name == "ClientOnlyAttribute");

        // 获取方法参数
        string parameterList = string.Join(", ", method.Parameters.Select(p => $"{p.Type} {p.Name}"));
        string argumentList = string.Join(", ", method.Parameters.Select(p => p.Name));

        // 获取返回类型
        string returnType = method.ReturnType.ToDisplayString();

        // 方法定义
        source.AppendLine($"        public {returnType} {methodName}({parameterList})");
        source.AppendLine("        {");

        // 实现方法体
        if (isSubscriptionMethod)
        {
            GenerateSubscriptionMethod(source, method);
        }
        else
        {
            GenerateRpcMethod(source, method);
        }

        // 闭合方法
        source.AppendLine("        }");
        source.AppendLine();
    }

    private void GenerateSubscriptionMethod(StringBuilder source, IMethodSymbol method)
    {
        // 确定事件名称
        string eventName = method.Name.StartsWith("SubscribeTo")
            ? method.Name.Substring("SubscribeTo".Length)
            : method.GetAttributes()
                  .FirstOrDefault(a => a.AttributeClass.Name == "SubscriptionMethodAttribute")
                  ?.NamedArguments.FirstOrDefault(na => na.Key == "EventName").Value.Value?.ToString()
              ?? method.Name;

        // 获取回调参数类型
        var handlerParam = method.Parameters.First();
        var eventType = ((INamedTypeSymbol)handlerParam.Type).TypeArguments.First();

        source.AppendLine(
            $"            return _messageChannel.SubscribeToEvent<{eventType}>(\"{eventName}\", {handlerParam.Name});");
    }

    private void GenerateRpcMethod(StringBuilder source, IMethodSymbol method)
    {
        string methodName = method.Name;
        string returnType = method.ReturnType.ToDisplayString();
        bool isVoidTask = returnType == "System.Threading.Tasks.Task";
        bool isGenericTask = returnType.StartsWith("System.Threading.Tasks.Task<");
        bool isValueTask = returnType.StartsWith("System.Threading.Tasks.ValueTask<");

        // 请求参数和取消令牌
        var parameters = method.Parameters.ToList();
        string requestParam = parameters.Count > 0 ? parameters[0].Name : "null";
        string cancellationToken = parameters.LastOrDefault()?.Type.Name == "CancellationToken"
            ? parameters.Last().Name
            : "CancellationToken.None";

        // 服务名和方法名
        source.AppendLine($"            string serviceName = \"{method.ContainingType.Name}\";");
        source.AppendLine($"            string methodName = \"{methodName}\";");

        if (isVoidTask)
        {
            source.AppendLine(
                $"            return _messageChannel.SendRequestAsync<object, EmptyResponse>(serviceName, methodName, {requestParam}, {cancellationToken});");
        }
        else if (isGenericTask)
        {
            string responseType = ((INamedTypeSymbol)method.ReturnType).TypeArguments[0].ToDisplayString();
            source.AppendLine(
                $"            return _messageChannel.SendRequestAsync<object, {responseType}>(serviceName, methodName, {requestParam}, {cancellationToken});");
        }
        else if (isValueTask)
        {
            string responseType = ((INamedTypeSymbol)method.ReturnType).TypeArguments[0].ToDisplayString();
            source.AppendLine(
                $"            var task = _messageChannel.SendRequestAsync<object, {responseType}>(serviceName, methodName, {requestParam}, {cancellationToken});");
            source.AppendLine($"            return new ValueTask<{responseType}>(task);");
        }
        else
        {
            source.AppendLine(
                "            throw new NotSupportedException(\"Only Task, Task<T> and ValueTask<T> return types are supported\");");
        }
    }

    /// <summary>
    /// 语法接收器，用于识别标记有[GameService]的接口
    /// </summary>
    private class SyntaxReceiver : ISyntaxContextReceiver
    {
        public List<INamedTypeSymbol> CandidateInterfaces { get; } = new List<INamedTypeSymbol>();

        public void OnVisitSyntaxNode(GeneratorSyntaxContext context)
        {
            // 寻找接口声明
            if (context.Node is InterfaceDeclarationSyntax interfaceDeclaration)
            {
                // 获取语义模型中的符号
                var symbol = context.SemanticModel.GetDeclaredSymbol(interfaceDeclaration);
                if (symbol == null) return;

                // 检查是否有[GameService]特性
                if (symbol.GetAttributes().Any(a => a.AttributeClass.Name == "GameServiceAttribute"))
                {
                    CandidateInterfaces.Add(symbol);
                }
            }
        }
    }
}
