    private static void GenerateEventMetrics(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 事件指标收集器 - 线程安全的性能监控");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class EventMetrics");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly ConcurrentDictionary<string, EventMethodMetrics> _methodMetrics = new();");
        sb.AppendLine("        private long _totalEventsReceived;");
        sb.AppendLine("        private long _totalEventsProcessed;");
        sb.AppendLine("        private long _totalErrors;");
        sb.AppendLine("        private long _totalBatchesProcessed;");
        sb.AppendLine("        private long _totalBatchErrors;");
        sb.AppendLine();

        sb.AppendLine("        public void RecordEventReceived(string methodName)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalEventsReceived);");
        sb.AppendLine("            var metrics = _methodMetrics.GetOrAdd(methodName, _ => new EventMethodMetrics());");
        sb.AppendLine("            Interlocked.Increment(ref metrics.CallCount);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordEventProcessed(string methodName, TimeSpan duration)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalEventsProcessed);");
        sb.AppendLine("            var metrics = _methodMetrics.GetOrAdd(methodName, _ => new EventMethodMetrics());");
        sb.AppendLine("            Interlocked.Increment(ref metrics.SuccessCount);");
        sb.AppendLine("            ");
        sb.AppendLine("            // 更新延迟统计");
        sb.AppendLine("            var durationMs = duration.TotalMilliseconds;");
        sb.AppendLine("            lock (metrics)");
        sb.AppendLine("            {");
        sb.AppendLine("                metrics.TotalDuration += durationMs;");
        sb.AppendLine("                metrics.MaxDuration = Math.Max(metrics.MaxDuration, durationMs);");
        sb.AppendLine("                metrics.MinDuration = metrics.MinDuration == 0 ? durationMs : Math.Min(metrics.MinDuration, durationMs);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordEventError(string methodName, Exception exception, TimeSpan duration)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalErrors);");
        sb.AppendLine("            var metrics = _methodMetrics.GetOrAdd(methodName, _ => new EventMethodMetrics());");
        sb.AppendLine("            Interlocked.Increment(ref metrics.ErrorCount);");
        sb.AppendLine("            ");
        sb.AppendLine("            lock (metrics)");
        sb.AppendLine("            {");
        sb.AppendLine("                metrics.LastError = exception;");
        sb.AppendLine("                metrics.LastErrorTime = DateTime.UtcNow;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordBatchProcessed(int batchSize, int successCount, TimeSpan duration)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalBatchesProcessed);");
        sb.AppendLine("            Interlocked.Add(ref _totalEventsProcessed, successCount);");
        sb.AppendLine("            ");
        sb.AppendLine("            if (successCount < batchSize)");
        sb.AppendLine("            {");
        sb.AppendLine("                Interlocked.Add(ref _totalErrors, batchSize - successCount);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordBatchProcessingError(Exception exception)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalBatchErrors);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordSubscriptionError(Exception exception)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _totalErrors);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public EventHandlerMetrics GetSnapshot()");
        sb.AppendLine("        {");
        sb.AppendLine("            var methodSnapshots = new Dictionary<string, MethodMetrics>();");
        sb.AppendLine("            ");
        sb.AppendLine("            foreach (var kvp in _methodMetrics)");
        sb.AppendLine("            {");
        sb.AppendLine("                var method = kvp.Value;");
        sb.AppendLine("                lock (method)");
        sb.AppendLine("                {");
        sb.AppendLine("                    methodSnapshots[kvp.Key] = new MethodMetrics");
        sb.AppendLine("                    {");
        sb.AppendLine("                        CallCount = method.CallCount,");
        sb.AppendLine("                        SuccessCount = method.SuccessCount,");
        sb.AppendLine("                        ErrorCount = method.ErrorCount,");
        sb.AppendLine("                        AverageDuration = method.SuccessCount > 0 ? method.TotalDuration / method.SuccessCount : 0,");
        sb.AppendLine("                        MaxDuration = method.MaxDuration,");
        sb.AppendLine("                        MinDuration = method.MinDuration,");
        sb.AppendLine("                        LastError = method.LastError,");
        sb.AppendLine("                        LastErrorTime = method.LastErrorTime");
        sb.AppendLine("                    };");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            return new EventHandlerMetrics");
        sb.AppendLine("            {");
        sb.AppendLine("                TotalEventsReceived = _totalEventsReceived,");
        sb.AppendLine("                TotalEventsProcessed = _totalEventsProcessed,");
        sb.AppendLine("                TotalErrors = _totalErrors,");
        sb.AppendLine("                TotalBatchesProcessed = _totalBatchesProcessed,");
        sb.AppendLine("                TotalBatchErrors = _totalBatchErrors,");
        sb.AppendLine("                MethodMetrics = methodSnapshots");
        sb.AppendLine("            };");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private sealed class EventMethodMetrics");
        sb.AppendLine("        {");
        sb.AppendLine("            public long CallCount;");
        sb.AppendLine("            public long SuccessCount;");
        sb.AppendLine("            public long ErrorCount;");
        sb.AppendLine("            public double TotalDuration;");
        sb.AppendLine("            public double MaxDuration;");
        sb.AppendLine("            public double MinDuration;");
        sb.AppendLine("            public Exception? LastError;");
        sb.AppendLine("            public DateTime LastErrorTime;");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

