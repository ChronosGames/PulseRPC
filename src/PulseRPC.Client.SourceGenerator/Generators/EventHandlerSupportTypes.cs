        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class MonitoredEventReceiver<T> where T : class");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly T _inner;");
        sb.AppendLine("        private readonly SubscriptionContext _context;");
        sb.AppendLine();

        sb.AppendLine("        public MonitoredEventReceiver(T inner, SubscriptionContext context)");
        sb.AppendLine("        {");
        sb.AppendLine("            _inner = inner ?? throw new ArgumentNullException(nameof(inner));");
        sb.AppendLine("            _context = context ?? throw new ArgumentNullException(nameof(context));");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public T Inner => _inner;");
        sb.AppendLine("        public SubscriptionContext Context => _context;");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 创建监控方法调用");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public async Task InvokeMethodAsync(string methodName, Func<T, Task> method)");
        sb.AppendLine("        {");
        sb.AppendLine("            var stopwatch = Stopwatch.StartNew();");
        sb.AppendLine("            _context.Metrics.RecordEventReceived(methodName);");
        sb.AppendLine();
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                await method(_inner).ConfigureAwait(false);");
        sb.AppendLine("                _context.Metrics.RecordEventProcessed(methodName, stopwatch.Elapsed);");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (Exception ex)");
        sb.AppendLine("            {");
        sb.AppendLine("                _context.Metrics.RecordEventError(methodName, ex, stopwatch.Elapsed);");
        sb.AppendLine("                throw;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 创建监控方法调用（同步版本）");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public void InvokeMethod(string methodName, Action<T> method)");
        sb.AppendLine("        {");
        sb.AppendLine("            var stopwatch = Stopwatch.StartNew();");
        sb.AppendLine("            _context.Metrics.RecordEventReceived(methodName);");
        sb.AppendLine();
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                method(_inner);");
        sb.AppendLine("                _context.Metrics.RecordEventProcessed(methodName, stopwatch.Elapsed);");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (Exception ex)");
        sb.AppendLine("            {");
        sb.AppendLine("                _context.Metrics.RecordEventError(methodName, ex, stopwatch.Elapsed);");
        sb.AppendLine("                throw;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void GenerateCircuitBreakerState(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 断路器状态");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class CircuitBreakerState : IDisposable");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly object _lock = new();");
        sb.AppendLine("        private int _failureCount;");
        sb.AppendLine("        private DateTime _lastFailureTime;");
        sb.AppendLine("        private bool _isOpen;");
        sb.AppendLine();

        sb.AppendLine("        public bool IsOpen");
        sb.AppendLine("        {");
        sb.AppendLine("            get");
        sb.AppendLine("            {");
        sb.AppendLine("                lock (_lock)");
        sb.AppendLine("                {");
        sb.AppendLine("                    if (_isOpen && DateTime.UtcNow - _lastFailureTime > TimeSpan.FromMinutes(1))");
        sb.AppendLine("                    {");
        sb.AppendLine("                        _isOpen = false;");
        sb.AppendLine("                        _failureCount = 0;");
        sb.AppendLine("                    }");
        sb.AppendLine("                    return _isOpen;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordSuccess()");
        sb.AppendLine("        {");
        sb.AppendLine("            lock (_lock)");
        sb.AppendLine("            {");
        sb.AppendLine("                _failureCount = 0;");
        sb.AppendLine("                _isOpen = false;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordFailure()");
        sb.AppendLine("        {");
        sb.AppendLine("            lock (_lock)");
        sb.AppendLine("            {");
        sb.AppendLine("                _failureCount++;");
        sb.AppendLine("                _lastFailureTime = DateTime.UtcNow;");
        sb.AppendLine("                if (_failureCount >= 5)");
        sb.AppendLine("                {");
        sb.AppendLine("                    _isOpen = true;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void Dispose()");
        sb.AppendLine("        {");
        sb.AppendLine("            // 清理资源");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void GenerateAsyncHelpers(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 异步方法辅助扩展 - 为监控包装器提供异步支持");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal static class AsyncMethodHelpers");
        sb.AppendLine("    {");

        // Task 包装方法
        sb.AppendLine("        [System.Runtime.CompilerServices.MethodImpl(System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        public static async Task CompleteTaskWithMetrics(Task task, string methodName, System.Diagnostics.Stopwatch stopwatch)");
        sb.AppendLine("        {");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                await task.ConfigureAwait(false);");
        sb.AppendLine("            }");
        sb.AppendLine("            finally");
        sb.AppendLine("            {");
        sb.AppendLine("                stopwatch.Stop();");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        // ValueTask 包装方法
        sb.AppendLine("        [System.Runtime.CompilerServices.MethodImpl(System.Runtime.CompilerServices.MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        public static async System.Threading.Tasks.ValueTask CompleteValueTaskWithMetrics(System.Threading.Tasks.ValueTask valueTask, string methodName, System.Diagnostics.Stopwatch stopwatch)");
        sb.AppendLine("        {");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                await valueTask.ConfigureAwait(false);");
        sb.AppendLine("            }");
        sb.AppendLine("            finally");
        sb.AppendLine("            {");
        sb.AppendLine("                stopwatch.Stop();");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 重试逻辑
        sb.AppendLine("        public static async Task<T> WithRetryAsync<T>(Func<Task<T>> operation, int maxRetries, TimeSpan delay)");
        sb.AppendLine("        {");
        sb.AppendLine("            Exception? lastException = null;");
        sb.AppendLine("            ");
        sb.AppendLine("            for (int attempt = 0; attempt <= maxRetries; attempt++)");
        sb.AppendLine("            {");
        sb.AppendLine("                try");
        sb.AppendLine("                {");
        sb.AppendLine("                    return await operation().ConfigureAwait(false);");
        sb.AppendLine("                }");
        sb.AppendLine("                catch (Exception ex)");
        sb.AppendLine("                {");
        sb.AppendLine("                    lastException = ex;");
        sb.AppendLine("                    if (attempt == maxRetries)");
        sb.AppendLine("                        break;");
        sb.AppendLine("                    await Task.Delay(delay).ConfigureAwait(false);");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("            throw lastException!;");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("    }");

        // 结束命名空间
        sb.AppendLine("}");
    }
