    private static void GenerateBatchProcessor(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 客户端批量事件处理器 - 优化高频事件处理性能");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class BatchProcessor : IDisposable");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly EventMetrics _metrics;");
        sb.AppendLine("        private readonly System.Threading.Channels.Channel<BatchEventItem> _eventQueue;");
        sb.AppendLine("        private readonly Task _processingTask;");
        sb.AppendLine("        private readonly CancellationTokenSource _cancellationTokenSource = new();");
        sb.AppendLine("        private volatile bool _disposed;");
        sb.AppendLine();

        sb.AppendLine("        public BatchProcessor(EventMetrics metrics)");
        sb.AppendLine("        {");
        sb.AppendLine("            _metrics = metrics ?? throw new ArgumentNullException(nameof(metrics));");
        sb.AppendLine("            var options = new System.Threading.Channels.BoundedChannelOptions(1000)");
        sb.AppendLine("            {");
        sb.AppendLine("                FullMode = System.Threading.Channels.BoundedChannelFullMode.Wait,");
        sb.AppendLine("                SingleReader = true,");
        sb.AppendLine("                SingleWriter = false");
        sb.AppendLine("            };");
        sb.AppendLine("            _eventQueue = System.Threading.Channels.Channel.CreateBounded<BatchEventItem>(options);");
        sb.AppendLine("            _processingTask = ProcessBatchEventsAsync(_cancellationTokenSource.Token);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public async ValueTask QueueEventAsync<T>(string eventName, T eventData, Func<T, Task> handler)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (_disposed) return;");
        sb.AppendLine("            ");
        sb.AppendLine("            var item = new BatchEventItem(eventName, eventData!, async data =>");
        sb.AppendLine("            {");
        sb.AppendLine("                if (data is T typedData)");
        sb.AppendLine("                {");
        sb.AppendLine("                    await handler(typedData).ConfigureAwait(false);");
        sb.AppendLine("                }");
        sb.AppendLine("            });");
        sb.AppendLine("            ");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                await _eventQueue.Writer.WriteAsync(item, _cancellationTokenSource.Token).ConfigureAwait(false);");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (OperationCanceledException)");
        sb.AppendLine("            {");
        sb.AppendLine("                // 已取消，忽略");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private async Task ProcessBatchEventsAsync(CancellationToken cancellationToken)");
        sb.AppendLine("        {");
        sb.AppendLine("            var batch = new List<BatchEventItem>(50);");
        sb.AppendLine("            ");
        sb.AppendLine("            while (!cancellationToken.IsCancellationRequested)");
        sb.AppendLine("            {");
        sb.AppendLine("                try");
        sb.AppendLine("                {");
        sb.AppendLine("                    // 收集事件到批次中");
        sb.AppendLine("                    while (batch.Count < 50 && _eventQueue.Reader.TryRead(out var item))");
        sb.AppendLine("                    {");
        sb.AppendLine("                        batch.Add(item);");
        sb.AppendLine("                    }");
        sb.AppendLine();
        sb.AppendLine("                    if (batch.Count > 0)");
        sb.AppendLine("                    {");
        sb.AppendLine("                        await ProcessBatch(batch).ConfigureAwait(false);");
        sb.AppendLine("                        batch.Clear();");
        sb.AppendLine("                    }");
        sb.AppendLine("                    else");
        sb.AppendLine("                    {");
        sb.AppendLine("                        // 等待新事件");
        sb.AppendLine("                        await Task.Delay(10, cancellationToken).ConfigureAwait(false);");
        sb.AppendLine("                    }");
        sb.AppendLine("                }");
        sb.AppendLine("                catch (OperationCanceledException)");
        sb.AppendLine("                {");
        sb.AppendLine("                    break;");
        sb.AppendLine("                }");
        sb.AppendLine("                catch (Exception ex)");
        sb.AppendLine("                {");
        sb.AppendLine("                    _metrics.RecordBatchProcessingError(ex);");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private async Task ProcessBatch(List<BatchEventItem> batch)");
        sb.AppendLine("        {");
        sb.AppendLine("            var stopwatch = Stopwatch.StartNew();");
        sb.AppendLine("            ");
        sb.AppendLine("            var tasks = batch.Select(async item =>");
        sb.AppendLine("            {");
        sb.AppendLine("                try");
        sb.AppendLine("                {");
        sb.AppendLine("                    await item.Handler(item.EventData).ConfigureAwait(false);");
        sb.AppendLine("                    return true;");
        sb.AppendLine("                }");
        sb.AppendLine("                catch (Exception ex)");
        sb.AppendLine("                {");
        sb.AppendLine("                    _metrics.RecordEventError(item.EventName, ex, TimeSpan.Zero);");
        sb.AppendLine("                    return false;");
        sb.AppendLine("                }");
        sb.AppendLine("            }).ToArray();");
        sb.AppendLine("            ");
        sb.AppendLine("            var results = await Task.WhenAll(tasks).ConfigureAwait(false);");
        sb.AppendLine("            var successCount = results.Count(r => r);");
        sb.AppendLine("            ");
        sb.AppendLine("            _metrics.RecordBatchProcessed(batch.Count, successCount, stopwatch.Elapsed);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void Dispose()");
        sb.AppendLine("        {");
        sb.AppendLine("            if (_disposed) return;");
        sb.AppendLine("            _disposed = true;");
        sb.AppendLine("            ");
        sb.AppendLine("            _cancellationTokenSource.Cancel();");
        sb.AppendLine("            _eventQueue.Writer.Complete();");
        sb.AppendLine("            ");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                _processingTask.Wait(TimeSpan.FromSeconds(5));");
        sb.AppendLine("            }");
        sb.AppendLine("            catch (AggregateException)");
        sb.AppendLine("            {");
        sb.AppendLine("                // 忽略超时异常");
        sb.AppendLine("            }");
        sb.AppendLine("            ");
        sb.AppendLine("            _cancellationTokenSource.Dispose();");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

