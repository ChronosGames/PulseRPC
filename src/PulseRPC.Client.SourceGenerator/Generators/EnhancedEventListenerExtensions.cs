    private static void GenerateConditionalSubscriptionMethods(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Conditional Subscription Methods");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 条件性注册事件监听器 - 只有当条件满足时才订阅");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<ISubscriptionToken?> RegisterEventListenerIfAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            Func<bool> condition,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            if (condition())");
        sb.AppendLine("            {");
        sb.AppendLine("                return await client.RegisterSmartEventListenerAsync(listener, options);");
        sb.AppendLine("            }");
        sb.AppendLine("            return null;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 延迟注册事件监听器 - 在指定延迟后订阅");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<ISubscriptionToken> RegisterEventListenerDelayedAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            TimeSpan delay,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            await Task.Delay(delay);");
        sb.AppendLine("            return await client.RegisterSmartEventListenerAsync(listener, options);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 临时注册事件监听器 - 在指定时间后自动取消订阅");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<ISubscriptionToken> RegisterEventListenerTemporaryAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            TimeSpan duration,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            var token = await client.RegisterSmartEventListenerAsync(listener, options);");
        sb.AppendLine();
        sb.AppendLine("            // 启动定时器在指定时间后取消订阅");
        sb.AppendLine("            _ = Task.Run(async () =>");
        sb.AppendLine("            {");
        sb.AppendLine("                await Task.Delay(duration);");
        sb.AppendLine("                token.Unsubscribe();");
        sb.AppendLine("            });");
        sb.AppendLine();
        sb.AppendLine("            return token;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
        sb.AppendLine();

        // 生成核心的智能注册方法
        GenerateCoreSmartRegistrationMethod(sb, eventTypes);
    }

    private static void GenerateCoreSmartRegistrationMethod(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Core Smart Registration");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 核心智能事件监听器注册方法 - 支持所有高级功能");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<ISubscriptionToken> RegisterSmartEventListenerAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (listener == null) throw new ArgumentNullException(nameof(listener));");
        sb.AppendLine();
        sb.AppendLine("            options ??= SmartSubscriptionOptions.Default;");
        sb.AppendLine();

        // 为每个事件类型生成智能分发逻辑
        sb.AppendLine("            // 智能分发到对应的处理器");
        foreach (var eventType in eventTypes)
        {
            if (eventType == null) continue;

            var interfaceName = eventType.Name;
            var handlerClassName = GetSmartHandlerClassName(interfaceName);
            var namespaceName = eventType.ContainingNamespace.ToDisplayString();

            sb.AppendLine($"            if (listener is {GetFullTypeName(eventType)} {interfaceName.ToLower()}Listener)");
            sb.AppendLine("            {");
            sb.AppendLine($"                var handler = new {namespaceName}.Smart{handlerClassName}();");
            sb.AppendLine($"                return await handler.SubscribeAsync({interfaceName.ToLower()}Listener, options);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        // 回退到传统方法
        sb.AppendLine("            // 回退到传统注册方法");
        sb.AppendLine("            return await client.RegisterEventListenerAsync(listener);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
    }

    // 辅助方法
    private static string GetSmartHandlerClassName(string interfaceName)
    {
        return interfaceName.StartsWith("I")
            ? interfaceName.Substring(1) + "Handler"
            : interfaceName + "Handler";
    }

    private static string GetCleanTypeName(string interfaceName)
    {
        return interfaceName.StartsWith("I") ? interfaceName.Substring(1) : interfaceName;
    }

    private static string GetFullTypeName(INamedTypeSymbol typeSymbol)
    {
        if (typeSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            return typeSymbol.Name;
        }
        return $"{typeSymbol.ContainingNamespace.ToDisplayString()}.{typeSymbol.Name}";
    }
