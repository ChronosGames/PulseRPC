    /// 生成增强的事件监听器扩展方法
    /// </summary>
    public static string GenerateEnhancedExtensions(ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        var sb = new StringBuilder();

        GenerateFileHeader(sb);
        GenerateExtensionsClass(sb, eventTypes);

        return sb.ToString();
    }

