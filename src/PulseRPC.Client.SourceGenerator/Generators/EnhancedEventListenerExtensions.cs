    }

    private static void GenerateExtensionsClass(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("namespace PulseRPC.Client");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 增强的事件监听器扩展 - 支持智能处理器、批量处理、高级配置");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static partial class EnhancedEventListenerExtensions");
        sb.AppendLine("    {");

        // 生成智能处理器工厂方法
        GenerateSmartHandlerFactories(sb, eventTypes);

        // 生成批量订阅方法
        GenerateBatchSubscriptionMethods(sb, eventTypes);

        // 生成预设配置方法
        GeneratePresetConfigurationMethods(sb, eventTypes);

        // 生成性能监控方法
        GeneratePerformanceMonitoringMethods(sb, eventTypes);

        // 生成条件订阅方法
        GenerateConditionalSubscriptionMethods(sb, eventTypes);

        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

    private static void GenerateSmartHandlerFactories(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Smart Handler Factory Methods");
        sb.AppendLine();

        foreach (var eventType in eventTypes)
        {
            if (eventType == null) continue;

            var interfaceName = eventType.Name;
            var handlerClassName = GetSmartHandlerClassName(interfaceName);
            var namespaceName = eventType.ContainingNamespace.ToDisplayString();

            sb.AppendLine($"        /// <summary>");
            sb.AppendLine($"        /// 创建 {interfaceName} 的智能事件处理器");
            sb.AppendLine($"        /// 包含性能监控、批量处理、错误恢复等高级功能");
            sb.AppendLine($"        /// </summary>");
            sb.AppendLine($"        public static {namespaceName}.Smart{handlerClassName} CreateSmart{GetCleanTypeName(interfaceName)}Handler(this IPulseClient client)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
            sb.AppendLine($"            return new {namespaceName}.Smart{handlerClassName}();");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        sb.AppendLine("        #endregion");
        sb.AppendLine();
    }

    private static void GenerateBatchSubscriptionMethods(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Batch Subscription Methods");
        sb.AppendLine();

        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 批量订阅多个事件监听器 - 优化大量订阅的性能");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static Task<ISubscriptionToken[]> BatchSubscribeAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            IEnumerable<T> listeners,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (listeners == null) throw new ArgumentNullException(nameof(listeners));");
        sb.AppendLine();
        sb.AppendLine("            var listenerList = listeners.ToList();");
        sb.AppendLine("            if (listenerList.Count == 0) return Task.FromResult(Array.Empty<ISubscriptionToken>());");
        sb.AppendLine();
        sb.AppendLine("            // 并行订阅以提高性能");
        sb.AppendLine("            var tasks = listenerList.Select(listener => client.RegisterSmartEventListenerAsync(listener, options));");
        sb.AppendLine("            return Task.WhenAll(tasks);");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成按类型分组的批量订阅
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 按事件类型分组批量订阅 - 自动选择最佳的智能处理器");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<IDictionary<Type, IList<ISubscriptionToken>>> BatchSubscribeByTypeAsync(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            IEnumerable<IPulseReceiver> listeners,");
        sb.AppendLine("            SmartSubscriptionOptions? options = null)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (listeners == null) throw new ArgumentNullException(nameof(listeners));");
        sb.AppendLine();
        sb.AppendLine("            var result = new Dictionary<Type, IList<ISubscriptionToken>>();");
        sb.AppendLine("            var groupedListeners = listeners.GroupBy(l => l.GetType());");
        sb.AppendLine();
        sb.AppendLine("            var tasks = groupedListeners.Select(async group =>");
        sb.AppendLine("            {");
        sb.AppendLine("                var tokens = await client.BatchSubscribeAsync(group, options);");
        sb.AppendLine("                return new { Type = group.Key, Tokens = tokens };");
        sb.AppendLine("            });");
        sb.AppendLine();
        sb.AppendLine("            var results = await Task.WhenAll(tasks);");
        sb.AppendLine("            foreach (var item in results)");
        sb.AppendLine("            {");
        sb.AppendLine("                result[item.Type] = item.Tokens;");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            return result;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
        sb.AppendLine();
    }

