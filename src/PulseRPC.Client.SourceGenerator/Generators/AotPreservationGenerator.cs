using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace PulseRPC.Generator.Generators;

/// <summary>
/// 生成 Unity linker 可识别的根，并显式闭合契约实际使用的泛型调用。
/// </summary>
public static class AotPreservationGenerator
{
    private static readonly SymbolDisplayFormat TypeDisplayFormat =
        SymbolDisplayFormat.FullyQualifiedFormat.WithMiscellaneousOptions(
            SymbolDisplayFormat.FullyQualifiedFormat.MiscellaneousOptions |
            SymbolDisplayMiscellaneousOptions.IncludeNullableReferenceTypeModifier);

    public static string Generate(
        ImmutableArray<INamedTypeSymbol> hubTypes,
        ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        var payloadTypes = CollectPayloadTypes(hubTypes, receiverTypes);
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// Deterministic Unity IL2CPP/AOT roots generated by PulseRPC.Client.SourceGenerator");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("namespace PulseRPC.Client.Generated");
        sb.AppendLine("{");
        AppendPreserveAttribute(sb, "    ");
        sb.AppendLine("    internal static class PulseRPCAotPreservation");
        sb.AppendLine("    {");

        GenerateMemoryPackRoots(sb, payloadTypes);
        GenerateHubRoots(sb, hubTypes);
        GenerateReceiverRoots(sb, receiverTypes);

        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static IReadOnlyList<string> CollectPayloadTypes(
        ImmutableArray<INamedTypeSymbol> hubTypes,
        ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        var result = new HashSet<string>(System.StringComparer.Ordinal);

        foreach (var hubType in hubTypes)
        {
            foreach (var method in GetAllInterfaceMethods(hubType))
            {
                AddWirePayloadType(result, method, useEmptyResponseForNoParameters: true);
                AddTaskResultType(result, method);
            }
        }

        foreach (var receiverType in receiverTypes)
        {
            foreach (var method in GetAllInterfaceMethods(receiverType))
            {
                AddWirePayloadType(result, method, useEmptyResponseForNoParameters: false);
                AddTaskResultType(result, method);
            }
        }

        return result.OrderBy(type => type, System.StringComparer.Ordinal).ToArray();
    }

    private static void AddWirePayloadType(
        ISet<string> payloadTypes,
        IMethodSymbol method,
        bool useEmptyResponseForNoParameters)
    {
        var wireParameters = method.Parameters
            .Where(parameter => !IsCancellationToken(parameter.Type))
            .ToArray();

        if (wireParameters.Length == 0)
        {
            if (useEmptyResponseForNoParameters)
            {
                payloadTypes.Add("global::PulseRPC.EmptyResponse");
            }
            return;
        }

        if (wireParameters.Length == 1)
        {
            payloadTypes.Add(wireParameters[0].Type.ToDisplayString(TypeDisplayFormat));
            return;
        }

        payloadTypes.Add("(" + string.Join(", ", wireParameters.Select(
            parameter => parameter.Type.ToDisplayString(TypeDisplayFormat))) + ")");
    }

    private static void AddTaskResultType(ISet<string> payloadTypes, IMethodSymbol method)
    {
        if (method.ReturnType is not INamedTypeSymbol namedReturnType ||
            !namedReturnType.IsGenericType ||
            namedReturnType.TypeArguments.Length != 1 ||
            namedReturnType.ConstructedFrom.Name is not ("Task" or "ValueTask"))
        {
            return;
        }

        payloadTypes.Add(namedReturnType.TypeArguments[0].ToDisplayString(TypeDisplayFormat));
    }

    private static IEnumerable<IMethodSymbol> GetAllInterfaceMethods(INamedTypeSymbol interfaceType)
    {
        return interfaceType.GetMembers().OfType<IMethodSymbol>()
            .Concat(interfaceType.AllInterfaces.SelectMany(type => type.GetMembers().OfType<IMethodSymbol>()))
            .Where(method => method.MethodKind == MethodKind.Ordinary && method.DeclaredAccessibility == Accessibility.Public)
            .GroupBy(MethodIdentity.CreateLookupKey, System.StringComparer.Ordinal)
            .Select(group => group.First());
    }

    private static bool IsCancellationToken(ITypeSymbol type)
        => type.ToDisplayString() is "System.Threading.CancellationToken" or "CancellationToken";

    private static void GenerateMemoryPackRoots(StringBuilder sb, IReadOnlyList<string> payloadTypes)
    {
        AppendPreserveAttribute(sb, "        ");
        sb.AppendLine("        private static void PreserveMemoryPackClosures()");
        sb.AppendLine("        {");
        sb.AppendLine("            var __buffer__ = new System.Buffers.ArrayBufferWriter<byte>();");

        foreach (var payloadType in payloadTypes)
        {
            sb.AppendLine($"            MemoryPack.MemoryPackSerializer.Serialize(__buffer__, default({payloadType}));");
            sb.AppendLine($"            _ = MemoryPack.MemoryPackSerializer.Deserialize<{payloadType}>(System.ReadOnlySpan<byte>.Empty);");
        }

        sb.AppendLine("        }");
        sb.AppendLine();
    }

    private static void GenerateHubRoots(StringBuilder sb, ImmutableArray<INamedTypeSymbol> hubTypes)
    {
        var orderedHubTypes = hubTypes
            .OrderBy(type => type.ToDisplayString(TypeDisplayFormat), System.StringComparer.Ordinal)
            .ToArray();

        for (var i = 0; i < orderedHubTypes.Length; i++)
        {
            var hubType = orderedHubTypes[i].ToDisplayString(TypeDisplayFormat);
            var stubType = hubType + "Stub";

            AppendPreserveAttribute(sb, "        ");
            sb.AppendLine($"        private static {hubType} PreserveHub{i}(global::PulseRPC.Client.IClientChannel channel)");
            sb.AppendLine("        {");
            sb.AppendLine($"            _ = typeof({stubType});");
            sb.AppendLine($"            return global::PulseRPC.Client.ClientChannelGenericExtensions.GetHub<{hubType}>(channel);");
            sb.AppendLine("        }");
            sb.AppendLine();
        }
    }

    private static void GenerateReceiverRoots(StringBuilder sb, ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        var orderedReceiverTypes = receiverTypes
            .OrderBy(type => type.ToDisplayString(TypeDisplayFormat), System.StringComparer.Ordinal)
            .ToArray();

        for (var i = 0; i < orderedReceiverTypes.Length; i++)
        {
            var receiver = orderedReceiverTypes[i];
            var receiverType = receiver.ToDisplayString(TypeDisplayFormat);
            var dispatcherName = receiver.Name.StartsWith("I", System.StringComparison.Ordinal)
                ? receiver.Name.Substring(1) + "Dispatcher"
                : receiver.Name + "Dispatcher";
            var dispatcherType = receiver.ContainingNamespace.IsGlobalNamespace
                ? "global::" + dispatcherName
                : "global::" + receiver.ContainingNamespace.ToDisplayString() + "." + dispatcherName;

            AppendPreserveAttribute(sb, "        ");
            sb.AppendLine($"        private static global::PulseRPC.ISubscriptionToken PreserveReceiver{i}(");
            sb.AppendLine("            global::PulseRPC.Client.IClientChannel channel,");
            sb.AppendLine($"            {receiverType} receiver)");
            sb.AppendLine("        {");
            sb.AppendLine($"            _ = typeof({dispatcherType});");
            sb.AppendLine($"            return global::PulseRPC.Client.ClientChannelGenericExtensions.RegisterReceiver<{receiverType}>(channel, receiver);");
            sb.AppendLine("        }");
            sb.AppendLine();
        }
    }

    private static void AppendPreserveAttribute(StringBuilder sb, string indent)
    {
        sb.AppendLine("#if UNITY_2019_1_OR_NEWER");
        sb.AppendLine(indent + "[UnityEngine.Scripting.Preserve]");
        sb.AppendLine("#endif");
    }
}
