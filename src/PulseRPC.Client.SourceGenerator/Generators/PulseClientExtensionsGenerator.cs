using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;

namespace PulseRPC.Generator.Generators;

/// <summary>
/// 生成 PulseClient 的工厂模式扩展方法，包含 GetServiceAsync 和 RegisterEventListenerAsync 的实现
/// </summary>
public static class PulseClientExtensionsGenerator
{
    /// <summary>
    /// 生成 PulseClient 扩展方法，提供工厂模式的服务和事件监听器注册
    /// </summary>
    public static string GeneratePulseClientExtensions(
        ImmutableArray<INamedTypeSymbol> serviceTypes,
        ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        var sb = new StringBuilder();

        GenerateFileHeader(sb);
        GenerateExtensionsClass(sb, serviceTypes, eventTypes);

        return sb.ToString();
    }

    private static void GenerateFileHeader(StringBuilder sb)
    {
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// PulseClient Factory Pattern Extensions - Generated Implementation");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS1591");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Linq;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using PulseRPC;");
        sb.AppendLine("using PulseRPC.Client;");
        sb.AppendLine("using PulseRPC.Client.Configuration;");
        sb.AppendLine();
    }

    private static void GenerateExtensionsClass(StringBuilder sb,
        ImmutableArray<INamedTypeSymbol> serviceTypes,
        ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("namespace PulseRPC.Client");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// PulseClient 工厂模式扩展方法 - 提供类型安全的服务和事件监听器注册");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static partial class PulseClientFactoryExtensions");
        sb.AppendLine("    {");

        // 生成服务工厂方法
        GenerateServiceFactoryMethods(sb, serviceTypes);

        // 生成事件监听器工厂方法
        GenerateEventListenerFactoryMethods(sb, eventTypes);

        // 生成通用的类型分发器
        GenerateGenericTypeDispatcher(sb, serviceTypes, eventTypes);

        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

    private static void GenerateServiceFactoryMethods(StringBuilder sb, ImmutableArray<INamedTypeSymbol> serviceTypes)
    {
        sb.AppendLine("        #region Service Factory Methods");
        sb.AppendLine();

        // 生成通用的 GetServiceAsync<T> 实现
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 获取服务代理的通用工厂方法（自动路由到最佳连接）");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<T> GetServiceAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            ServiceProxyOptions? options = null,");
        sb.AppendLine("            CancellationToken cancellationToken = default)");
        sb.AppendLine("            where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine();
        sb.AppendLine("            var serviceType = typeof(T);");
        sb.AppendLine();

        // 为每个服务类型生成类型检查和代理创建逻辑
        foreach (var serviceType in serviceTypes)
        {
            if (serviceType == null) continue;

            var interfaceName = serviceType.Name;
            if (string.IsNullOrEmpty(interfaceName)) continue;

            var fullTypeName = GetFullTypeName(serviceType);
            if (string.IsNullOrEmpty(fullTypeName)) continue;

            var stubTypeName = $"{fullTypeName}Stub";

            sb.AppendLine($"            // 检查是否请求 {interfaceName} 服务（使用 IsAssignableFrom 支持接口实现类）");
            sb.AppendLine($"            if (typeof({fullTypeName}).IsAssignableFrom(serviceType))");
            sb.AppendLine("            {");
            sb.AppendLine($"                // 通过连接管理器路由到最佳连接");
            sb.AppendLine($"                var channel = await client.Connections.RouteAsync(\"{interfaceName}\", cancellationToken);");
            sb.AppendLine("                if (channel == null)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    throw new InvalidOperationException($\"无法找到 {interfaceName} 服务的可用连接\");");
            sb.AppendLine("                }");
            sb.AppendLine($"                return (T)(object)new {stubTypeName}(channel);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        // 如果没有匹配的类型，抛出异常
        sb.AppendLine("            throw new ArgumentException($\"不支持的服务类型: {serviceType.Name}。请确保服务接口已使用 [PulseClientGeneration] 特性注册。\", nameof(T));");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成指定连接的 GetServiceAsync<T> 实现
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 获取指定连接的服务 Stub 的工厂方法");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<T> GetServiceAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            string connectionId,");
        sb.AppendLine("            ServiceProxyOptions? options = null,");
        sb.AppendLine("            CancellationToken cancellationToken = default)");
        sb.AppendLine("            where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (string.IsNullOrEmpty(connectionId)) throw new ArgumentException(\"连接ID不能为空\", nameof(connectionId));");
        sb.AppendLine();
        sb.AppendLine("            var serviceType = typeof(T);");
        sb.AppendLine("            var connection = client.Connections.GetConnection(connectionId);");
        sb.AppendLine("            if (connection == null)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new ArgumentException($\"连接不存在: {connectionId}\", nameof(connectionId));");
        sb.AppendLine("            }");
        sb.AppendLine();

        // 为每个服务类型生成类型检查和 Stub 创建逻辑
        foreach (var serviceType in serviceTypes)
        {
            if (serviceType == null) continue;

            var interfaceName = serviceType.Name;
            if (string.IsNullOrEmpty(interfaceName)) continue;

            var fullTypeName = GetFullTypeName(serviceType);
            if (string.IsNullOrEmpty(fullTypeName)) continue;

            var stubTypeName = $"{fullTypeName}Stub";

            sb.AppendLine($"            // 检查是否请求 {interfaceName} 服务（使用 IsAssignableFrom 支持接口实现类）");
            sb.AppendLine($"            if (typeof({fullTypeName}).IsAssignableFrom(serviceType))");
            sb.AppendLine("            {");
            sb.AppendLine($"                return (T)(object)new {stubTypeName}(connection);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        // 如果没有匹配的类型，抛出异常
        sb.AppendLine("            throw new ArgumentException($\"不支持的服务类型: {serviceType.Name}。请确保服务接口已使用 [PulseClientGeneration] 特性注册。\", nameof(T));");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
        sb.AppendLine();
    }

    private static void GenerateEventListenerFactoryMethods(StringBuilder sb, ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Event Listener Factory Methods");
        sb.AppendLine();

        // 生成具象的 Register{ReceiverName}Async 方法
        // 临时禁用以定位问题
        // GenerateConcreteEventListenerMethods(sb, eventTypes);
        // sb.AppendLine();

        // 生成通用的 RegisterEventListenerAsync<T> 实现（自动路由）
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 注册事件监听器的通用工厂方法（自动路由到最佳连接）");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static async Task<ISubscriptionToken> RegisterEventListenerAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            EventListenerOptions? options = null,");
        sb.AppendLine("            CancellationToken cancellationToken = default)");
        sb.AppendLine("            where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (listener == null) throw new ArgumentNullException(nameof(listener));");
        sb.AppendLine();
        sb.AppendLine("            var listenerType = typeof(T);");
        sb.AppendLine("            var tokens = new List<ISubscriptionToken>();");
        sb.AppendLine();

        // 为每个事件类型生成类型检查和注册逻辑
        foreach (var eventType in eventTypes)
        {
            if (eventType == null) continue;

            var interfaceName = eventType.Name;
            if (string.IsNullOrEmpty(interfaceName)) continue;

            var fullTypeName = GetFullTypeName(eventType);
            if (string.IsNullOrEmpty(fullTypeName)) continue;

            sb.AppendLine($"            // 检查是否实现了 {interfaceName}");
            sb.AppendLine($"            if (listener is {fullTypeName} {interfaceName.ToLower()}Listener)");
            sb.AppendLine("            {");
            sb.AppendLine($"                // 通过连接管理器路由到最佳连接");
            sb.AppendLine($"                var connectionContext = await client.Connections.RouteAsync(\"{interfaceName}\", cancellationToken);");
            sb.AppendLine("                if (connectionContext == null)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    throw new InvalidOperationException($\"无法找到 {interfaceName} 事件监听的可用连接\");");
            sb.AppendLine("                }");
            sb.AppendLine($"                // 使用轻量级 Dispatcher 进行事件分发");

            // 安全地提取命名空间
            var namespaceName = fullTypeName!.Contains(".")
                ? fullTypeName.Substring(0, fullTypeName.LastIndexOf('.'))
                : string.Empty;

            var dispatcherClassName = GetDispatcherClassName(interfaceName);
            var fullDispatcherClassName = string.IsNullOrEmpty(namespaceName)
                ? dispatcherClassName
                : $"{namespaceName}.{dispatcherClassName}";

            sb.AppendLine($"                var dispatcher = new {fullDispatcherClassName}({interfaceName.ToLower()}Listener);");
            sb.AppendLine($"                var channelToken = dispatcher.RegisterTo(connectionContext);");
            sb.AppendLine($"                var token = new SubscriptionToken(Guid.NewGuid(), \"{interfaceName}\", typeof({fullTypeName}), () => {{ channelToken?.Dispose(); dispatcher?.Dispose(); }});");
            sb.AppendLine($"                tokens.Add(token);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            if (tokens.Count == 0)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new ArgumentException($\"类型 {listenerType.Name} 未实现任何已注册的事件监听器接口。请确保它实现了使用 [PulseClientGeneration] 特性注册的接口。\", nameof(listener));");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            return tokens.Count == 1 ? tokens[0] : new CompositeSubscriptionToken(tokens);");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成指定连接的 RegisterEventListenerAsync<T> 实现
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 在指定连接上注册事件监听器的工厂方法");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static Task<ISubscriptionToken> RegisterEventListenerAsync<T>(");
        sb.AppendLine("            this IPulseClient client,");
        sb.AppendLine("            string connectionId,");
        sb.AppendLine("            T listener,");
        sb.AppendLine("            EventListenerOptions? options = null,");
        sb.AppendLine("            CancellationToken cancellationToken = default)");
        sb.AppendLine("            where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            if (client == null) throw new ArgumentNullException(nameof(client));");
        sb.AppendLine("            if (string.IsNullOrEmpty(connectionId)) throw new ArgumentException(\"连接ID不能为空\", nameof(connectionId));");
        sb.AppendLine("            if (listener == null) throw new ArgumentNullException(nameof(listener));");
        sb.AppendLine();
        sb.AppendLine("            var listenerType = typeof(T);");
        sb.AppendLine("            var connectionContext = client.Connections.GetConnection(connectionId);");
        sb.AppendLine("            if (connectionContext == null)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new ArgumentException($\"连接不存在: {connectionId}\", nameof(connectionId));");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            var tokens = new List<ISubscriptionToken>();");
        sb.AppendLine();

        // 为每个事件类型生成类型检查和注册逻辑
        foreach (var eventType in eventTypes)
        {
            if (eventType == null) continue;

            var interfaceName = eventType.Name;
            if (string.IsNullOrEmpty(interfaceName)) continue;

            var fullTypeName = GetFullTypeName(eventType);
            if (string.IsNullOrEmpty(fullTypeName)) continue;

            sb.AppendLine($"            // 检查是否实现了 {interfaceName}");
            sb.AppendLine($"            if (listener is {fullTypeName} {interfaceName.ToLower()}Listener)");
            sb.AppendLine("            {");
            var namespaceName = fullTypeName!.Contains(".")
                ? fullTypeName.Substring(0, fullTypeName.LastIndexOf('.'))
                : string.Empty;
            var dispatcherClassName = GetDispatcherClassName(interfaceName);
            var fullDispatcherClassName = string.IsNullOrEmpty(namespaceName)
                ? dispatcherClassName
                : $"{namespaceName}.{dispatcherClassName}";

            sb.AppendLine($"                var dispatcher = new {fullDispatcherClassName}({interfaceName.ToLower()}Listener);");
            sb.AppendLine("                var channelToken = dispatcher.RegisterTo(connectionContext);");
            sb.AppendLine($"                var token = new SubscriptionToken(Guid.NewGuid(), \"{interfaceName}\", typeof({fullTypeName}), () => {{ channelToken?.Dispose(); dispatcher?.Dispose(); }});");
            sb.AppendLine("                tokens.Add(token);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            if (tokens.Count == 0)");
        sb.AppendLine("            {");
        sb.AppendLine("                throw new ArgumentException($\"类型 {listenerType.Name} 未实现任何已注册的事件监听器接口。请确保它实现了使用 [PulseClientGeneration] 特性注册的接口。\", nameof(listener));");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            return Task.FromResult<ISubscriptionToken>(tokens.Count == 1 ? tokens[0] : new CompositeSubscriptionToken(tokens));");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
        sb.AppendLine();
    }

    private static void GenerateGenericTypeDispatcher(StringBuilder sb,
        ImmutableArray<INamedTypeSymbol> serviceTypes,
        ImmutableArray<INamedTypeSymbol> eventTypes)
    {
        sb.AppendLine("        #region Support Types");
        sb.AppendLine();

        // 生成组合订阅令牌
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 组合订阅令牌，管理多个事件订阅");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        private class CompositeSubscriptionToken : ISubscriptionToken");
        sb.AppendLine("        {");
        sb.AppendLine("            private readonly List<ISubscriptionToken> _tokens;");
        sb.AppendLine("            private bool _isDisposed;");
        sb.AppendLine();
        sb.AppendLine("            public Guid Id { get; } = Guid.NewGuid();");
        sb.AppendLine("            public bool IsActive => !_isDisposed && _tokens.Any(t => t.IsActive);");
        sb.AppendLine("            public bool IsUnsubscribed => _isDisposed;");
        sb.AppendLine();
        sb.AppendLine("            public CompositeSubscriptionToken(List<ISubscriptionToken> tokens)");
        sb.AppendLine("            {");
        sb.AppendLine("                _tokens = tokens ?? throw new ArgumentNullException(nameof(tokens));");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            public void Unsubscribe()");
        sb.AppendLine("            {");
        sb.AppendLine("                if (_isDisposed) return;");
        sb.AppendLine();
        sb.AppendLine("                foreach (var token in _tokens)");
        sb.AppendLine("                {");
        sb.AppendLine("                    try");
        sb.AppendLine("                    {");
        sb.AppendLine("                        token.Unsubscribe();");
        sb.AppendLine("                    }");
        sb.AppendLine("                    catch (Exception ex)");
        sb.AppendLine("                    {");
        sb.AppendLine("                        // 记录错误但继续取消其他订阅");
        sb.AppendLine("                        System.Diagnostics.Debug.WriteLine($\"Error unsubscribing token {token.Id}: {ex.Message}\");");
        sb.AppendLine("                    }");
        sb.AppendLine("                }");
        sb.AppendLine();
        sb.AppendLine("                _isDisposed = true;");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            public void Dispose()");
        sb.AppendLine("            {");
        sb.AppendLine("                Unsubscribe();");
        sb.AppendLine("                GC.SuppressFinalize(this);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        #endregion");
    }

    private static string GetDispatcherClassName(string interfaceName)
    {
        // 对于 IPlayerLoginEvents，应该生成 PlayerLoginEventsDispatcher（轻量级）
        var baseName = interfaceName.StartsWith("I")
            ? interfaceName.Substring(1)
            : interfaceName;
        return $"{baseName}Dispatcher";
    }

    private static string? GetFullTypeName(INamedTypeSymbol? typeSymbol)
    {
        if (typeSymbol == null) return null;
        if (typeSymbol.ContainingNamespace == null) return typeSymbol.Name;

        return typeSymbol.ContainingNamespace.IsGlobalNamespace
            ? typeSymbol.Name
            : $"{typeSymbol.ContainingNamespace.ToDisplayString()}.{typeSymbol.Name}";
    }
