using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace PulseRPC.Generator.Generators;

/// <summary>
/// 生成基于 IClientChannel 的泛型扩展方法
/// </summary>
/// <remarks>
/// 生成内容：
/// - GetHubAsync&lt;T&gt;(this IClientChannel channel) - 泛型获取 Hub 代理
/// - RegisterReceiver&lt;T&gt;(this IClientChannel channel, T receiver) - 泛型注册 Receiver
/// </remarks>
public static class ClientChannelGenericExtensionsGenerator
{
    /// <summary>
    /// 生成 IClientChannel 泛型扩展方法
    /// </summary>
    public static string Generate(
        ImmutableArray<INamedTypeSymbol> hubTypes,
        ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        var sb = new StringBuilder();

        GenerateFileHeader(sb);
        GenerateUsingStatements(sb, hubTypes, receiverTypes);

        sb.AppendLine("namespace PulseRPC.Client");
        sb.AppendLine("{");

        GenerateExtensionsClass(sb, hubTypes, receiverTypes);

        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void GenerateFileHeader(StringBuilder sb)
    {
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// This code was generated by PulseRPC.Client.SourceGenerator");
        sb.AppendLine("// IClientChannel generic extension methods");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
    }

    private static void GenerateUsingStatements(
        StringBuilder sb,
        ImmutableArray<INamedTypeSymbol> hubTypes,
        ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using PulseRPC;");
        sb.AppendLine("using PulseRPC.Client;");
        sb.AppendLine();

        // 收集所有需要的命名空间
        var namespaces = hubTypes
            .Concat(receiverTypes)
            .Where(t => t != null && !t.ContainingNamespace.IsGlobalNamespace)
            .Select(t => t.ContainingNamespace.ToDisplayString())
            .Distinct()
            .OrderBy(ns => ns);

        foreach (var ns in namespaces)
        {
            sb.AppendLine($"using {ns};");
        }

        sb.AppendLine();
    }

    private static void GenerateExtensionsClass(
        StringBuilder sb,
        ImmutableArray<INamedTypeSymbol> hubTypes,
        ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// IClientChannel 泛型扩展方法");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    /// <remarks>");
        sb.AppendLine("    /// 提供类型安全的泛型方法来获取 Hub 代理和注册 Receiver。");
        sb.AppendLine("    /// <code>");
        sb.AppendLine("    /// // 获取 Hub 代理");
        sb.AppendLine("    /// var hub = channel.GetHub&lt;IGameHub&gt;();");
        sb.AppendLine("    /// ");
        sb.AppendLine("    /// // 注册 Receiver");
        sb.AppendLine("    /// var token = channel.RegisterReceiver&lt;IGameEvents&gt;(myReceiver);");
        sb.AppendLine("    /// </code>");
        sb.AppendLine("    /// </remarks>");
        sb.AppendLine("    public static class ClientChannelGenericExtensions");
        sb.AppendLine("    {");

        // 生成 GetHub<T> 方法
        GenerateGetHubMethod(sb, hubTypes);

        // 生成 GetHubAsync<T> 方法
        GenerateGetHubAsyncMethod(sb, hubTypes);

        // 生成 RegisterReceiver<T> 方法
        GenerateRegisterReceiverMethod(sb, receiverTypes);

        sb.AppendLine("    }");
    }

    /// <summary>
    /// 生成同步的 GetHub&lt;T&gt; 方法
    /// </summary>
    private static void GenerateGetHubMethod(StringBuilder sb, ImmutableArray<INamedTypeSymbol> hubTypes)
    {
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 获取指定类型的 Hub 代理");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <typeparam name=\"T\">Hub 接口类型（必须实现 IPulseHub）</typeparam>");
        sb.AppendLine("        /// <param name=\"channel\">客户端通道</param>");
        sb.AppendLine("        /// <returns>Hub 代理实例</returns>");
        sb.AppendLine("        /// <exception cref=\"ArgumentException\">当类型 T 不是已注册的 Hub 接口时抛出</exception>");
        sb.AppendLine("        public static T GetHub<T>(this IClientChannel channel) where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            if (channel == null) throw new ArgumentNullException(nameof(channel));");
        sb.AppendLine();
        sb.AppendLine("            var hubType = typeof(T);");
        sb.AppendLine();

        // 为每个 Hub 类型生成类型检查（使用 IsAssignableFrom 支持接口实现类）
        foreach (var hubType in hubTypes)
        {
            if (hubType == null) continue;

            var fullTypeName = GetFullTypeName(hubType);
            var stubTypeName = $"{fullTypeName}Stub";

            sb.AppendLine($"            if (typeof({fullTypeName}).IsAssignableFrom(hubType))");
            sb.AppendLine("            {");
            sb.AppendLine($"                return (T)(object)new {stubTypeName}(channel);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            throw new ArgumentException($\"不支持的 Hub 类型: {hubType.Name}。请确保该接口已使用 [PulseClientGeneration] 特性注册。\", nameof(T));");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成异步的 GetHubAsync&lt;T&gt; 方法
    /// </summary>
    private static void GenerateGetHubAsyncMethod(StringBuilder sb, ImmutableArray<INamedTypeSymbol> hubTypes)
    {
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 异步获取指定类型的 Hub 代理");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <typeparam name=\"T\">Hub 接口类型（必须实现 IPulseHub）</typeparam>");
        sb.AppendLine("        /// <param name=\"channel\">客户端通道</param>");
        sb.AppendLine("        /// <param name=\"cancellationToken\">取消令牌</param>");
        sb.AppendLine("        /// <returns>Hub 代理实例</returns>");
        sb.AppendLine("        /// <exception cref=\"ArgumentException\">当类型 T 不是已注册的 Hub 接口时抛出</exception>");
        sb.AppendLine("        public static Task<T> GetHubAsync<T>(this IClientChannel channel, CancellationToken cancellationToken = default) where T : class, IPulseHub");
        sb.AppendLine("        {");
        sb.AppendLine("            // 当前实现是同步的，但保留异步签名以便未来扩展（如连接验证、服务发现等）");
        sb.AppendLine("            return Task.FromResult(channel.GetHub<T>());");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成 RegisterReceiver&lt;T&gt; 方法
    /// </summary>
    private static void GenerateRegisterReceiverMethod(StringBuilder sb, ImmutableArray<INamedTypeSymbol> receiverTypes)
    {
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 注册指定类型的 Receiver");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <typeparam name=\"T\">Receiver 接口类型（必须实现 IPulseReceiver）</typeparam>");
        sb.AppendLine("        /// <param name=\"channel\">客户端通道</param>");
        sb.AppendLine("        /// <param name=\"receiver\">Receiver 实现实例</param>");
        sb.AppendLine("        /// <returns>订阅令牌，用于取消注册</returns>");
        sb.AppendLine("        /// <exception cref=\"ArgumentException\">当类型 T 不是已注册的 Receiver 接口时抛出</exception>");
        sb.AppendLine("        public static ISubscriptionToken RegisterReceiver<T>(this IClientChannel channel, T receiver) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            if (channel == null) throw new ArgumentNullException(nameof(channel));");
        sb.AppendLine("            if (receiver == null) throw new ArgumentNullException(nameof(receiver));");
        sb.AppendLine();
        sb.AppendLine("            var receiverType = typeof(T);");
        sb.AppendLine();

        // 为每个 Receiver 类型生成类型检查（使用 IsAssignableFrom 支持接口实现类）
        foreach (var receiverType in receiverTypes)
        {
            if (receiverType == null) continue;

            var fullTypeName = GetFullTypeName(receiverType);
            var interfaceName = receiverType.Name;
            var dispatcherTypeName = GetDispatcherTypeName(interfaceName, fullTypeName);

            sb.AppendLine($"            if (typeof({fullTypeName}).IsAssignableFrom(receiverType))");
            sb.AppendLine("            {");
            sb.AppendLine($"                var dispatcher = new {dispatcherTypeName}(({fullTypeName})receiver);");
            sb.AppendLine($"                return dispatcher.RegisterTo(channel);");
            sb.AppendLine("            }");
            sb.AppendLine();
        }

        sb.AppendLine("            throw new ArgumentException($\"不支持的 Receiver 类型: {receiverType.Name}。请确保该接口已使用 [PulseClientGeneration] 特性注册。\", nameof(T));");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成异步版本的 RegisterReceiverAsync<T>
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 异步注册指定类型的 Receiver");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <typeparam name=\"T\">Receiver 接口类型（必须实现 IPulseReceiver）</typeparam>");
        sb.AppendLine("        /// <param name=\"channel\">客户端通道</param>");
        sb.AppendLine("        /// <param name=\"receiver\">Receiver 实现实例</param>");
        sb.AppendLine("        /// <param name=\"cancellationToken\">取消令牌</param>");
        sb.AppendLine("        /// <returns>订阅令牌，用于取消注册</returns>");
        sb.AppendLine("        public static Task<ISubscriptionToken> RegisterReceiverAsync<T>(");
        sb.AppendLine("            this IClientChannel channel,");
        sb.AppendLine("            T receiver,");
        sb.AppendLine("            CancellationToken cancellationToken = default) where T : class, IPulseReceiver");
        sb.AppendLine("        {");
        sb.AppendLine("            // 当前实现是同步的，但保留异步签名以便未来扩展");
        sb.AppendLine("            return Task.FromResult(channel.RegisterReceiver(receiver));");
        sb.AppendLine("        }");
    }

    private static string GetFullTypeName(INamedTypeSymbol typeSymbol)
    {
        if (typeSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            return typeSymbol.Name;
        }
        return $"{typeSymbol.ContainingNamespace.ToDisplayString()}.{typeSymbol.Name}";
    }

    private static string GetDispatcherTypeName(string interfaceName, string fullTypeName)
    {
        // IXxxReceiver -> XxxReceiverDispatcher
        var baseName = interfaceName.StartsWith("I") ? interfaceName.Substring(1) : interfaceName;
        var dispatcherName = $"{baseName}Dispatcher";

        // 如果有命名空间，替换接口名为 Dispatcher 名
        if (fullTypeName.Contains("."))
        {
            var ns = fullTypeName.Substring(0, fullTypeName.LastIndexOf('.'));
            return $"{ns}.{dispatcherName}";
        }

        return dispatcherName;
    }
}
