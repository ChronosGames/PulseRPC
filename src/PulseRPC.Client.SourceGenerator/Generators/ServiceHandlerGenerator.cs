using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace PulseRPC.Generator.Generators;

/// <summary>
/// 生成服务处理器（Service Handler），用于处理对本地 Hub 实现的远程调用
/// </summary>
public static class ServiceHandlerGenerator
{
    public static string GenerateServiceHandler(INamedTypeSymbol interfaceSymbol)
    {
        var interfaceName = interfaceSymbol.Name;
        var namespaceName = interfaceSymbol.ContainingNamespace.ToDisplayString();
        var handlerClassName = $"{interfaceName}ServiceHandler";

        var sb = new StringBuilder();

        // 生成文件头
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine($"// Generated Service Handler for {interfaceName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Buffers;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using PulseRPC;");
        sb.AppendLine("using PulseRPC.Channels;");
        sb.AppendLine("using MemoryPack;");
        sb.AppendLine();

        // 收集方法参数和返回值类型的命名空间
        var additionalNamespaces = CollectNamespacesFromMethods(interfaceSymbol);
        foreach (var ns in additionalNamespaces.OrderBy(x => x))
        {
            if (ns.StartsWith("System") || ns == namespaceName)
                continue;
            sb.AppendLine($"using {ns};");
        }

        sb.AppendLine();
        sb.AppendLine($"namespace {namespaceName}");
        sb.AppendLine("{");

        // 生成服务处理器类
        sb.AppendLine($"    /// <summary>");
        sb.AppendLine($"    /// {interfaceName} 的服务处理器 - 处理远程调用");
        sb.AppendLine($"    /// </summary>");
        sb.AppendLine($"    internal sealed class {handlerClassName} : IServiceHandler");
        sb.AppendLine("    {");
        sb.AppendLine($"        private readonly {interfaceName} _implementation;");
        sb.AppendLine();
        sb.AppendLine($"        public {handlerClassName}({interfaceName} implementation)");
        sb.AppendLine("        {");
        sb.AppendLine("            _implementation = implementation ?? throw new ArgumentNullException(nameof(implementation));");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成 HandleRequestAsync 方法
        GenerateHandleRequestMethod(sb, interfaceSymbol);

        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void GenerateHandleRequestMethod(StringBuilder sb, INamedTypeSymbol interfaceSymbol)
    {
        sb.AppendLine("        public async Task<object?> HandleRequestAsync(string methodName, ReadOnlyMemory<byte> parameters, IRequestContext context)");
        sb.AppendLine("        {");
        sb.AppendLine("            switch (methodName)");
        sb.AppendLine("            {");

        // 为每个方法生成 case 分支
        foreach (var member in interfaceSymbol.GetMembers())
        {
            if (member is not IMethodSymbol methodSymbol)
                continue;

            if (methodSymbol.DeclaredAccessibility != Accessibility.Public)
                continue;

            var methodName = methodSymbol.Name;
            var hasParameters = methodSymbol.Parameters.Length > 0;
            var isAsync = IsAsyncMethod(methodSymbol);
            var returnType = methodSymbol.ReturnType;

            sb.AppendLine($"                case \"{methodName}\":");
            sb.AppendLine("                {");

            // 反序列化参数
            if (hasParameters)
            {
                if (methodSymbol.Parameters.Length == 1)
                {
                    var paramType = methodSymbol.Parameters[0].Type.ToDisplayString();
                    sb.AppendLine($"                    var arg = MemoryPackSerializer.Deserialize<{paramType}>(parameters.Span)!;");
                }
                else
                {
                    // 多参数：生成元组类型
                    var paramTypes = string.Join(", ", methodSymbol.Parameters.Select(p => p.Type.ToDisplayString()));
                    sb.AppendLine($"                    var args = MemoryPackSerializer.Deserialize<({paramTypes})>(parameters.Span)!;");
                }
            }

            // 调用方法
            sb.Append("                    ");

            // 判断返回类型
            if (isAsync)
            {
                var taskReturnType = GetTaskReturnType(returnType);
                if (taskReturnType != null && taskReturnType.ToDisplayString() != "void")
                {
                    sb.Append("var result = ");
                }
                sb.Append("await _implementation.");
            }
            else
            {
                if (returnType.ToDisplayString() != "void")
                {
                    sb.Append("var result = ");
                }
                sb.Append("_implementation.");
            }

            sb.Append(methodName);
            sb.Append("(");

            // 传递参数
            if (hasParameters)
            {
                if (methodSymbol.Parameters.Length == 1)
                {
                    sb.Append("arg");
                }
                else
                {
                    for (int i = 0; i < methodSymbol.Parameters.Length; i++)
                    {
                        if (i > 0) sb.Append(", ");
                        sb.Append($"args.Item{i + 1}");
                    }
                }
            }

            sb.AppendLine(");");

            // 返回结果
            if (isAsync)
            {
                var taskReturnType = GetTaskReturnType(returnType);
                if (taskReturnType != null && taskReturnType.ToDisplayString() != "void")
                {
                    sb.AppendLine("                    return result;");
                }
                else
                {
                    sb.AppendLine("                    return null;");
                }
            }
            else
            {
                if (returnType.ToDisplayString() != "void")
                {
                    sb.AppendLine("                    return result;");
                }
                else
                {
                    sb.AppendLine("                    return null;");
                }
            }

            sb.AppendLine("                }");
        }

        sb.AppendLine("                default:");
        sb.AppendLine("                    throw new NotSupportedException($\"Method '{methodName}' is not supported\");");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
    }

    private static bool IsAsyncMethod(IMethodSymbol methodSymbol)
    {
        var returnType = methodSymbol.ReturnType;
        return returnType.Name == "Task" || returnType.Name == "ValueTask";
    }

    private static ITypeSymbol? GetTaskReturnType(ITypeSymbol returnType)
    {
        if (returnType is INamedTypeSymbol namedType && namedType.IsGenericType)
        {
            return namedType.TypeArguments.FirstOrDefault();
        }
        return null;
    }

    private static HashSet<string> CollectNamespacesFromMethods(INamedTypeSymbol interfaceSymbol)
    {
        var namespaces = new HashSet<string>();

        foreach (var member in interfaceSymbol.GetMembers())
        {
            if (member is not IMethodSymbol methodSymbol)
                continue;

            // 收集参数类型的命名空间
            foreach (var parameter in methodSymbol.Parameters)
            {
                AddNamespace(namespaces, parameter.Type);
            }

            // 收集返回类型的命名空间
            AddNamespace(namespaces, methodSymbol.ReturnType);
        }

        return namespaces;
    }

    private static void AddNamespace(HashSet<string> namespaces, ITypeSymbol typeSymbol)
    {
        if (typeSymbol is INamedTypeSymbol namedType)
        {
            if (!namedType.ContainingNamespace.IsGlobalNamespace)
            {
                namespaces.Add(namedType.ContainingNamespace.ToDisplayString());
            }

            // 处理泛型参数
            foreach (var typeArg in namedType.TypeArguments)
            {
                AddNamespace(namespaces, typeArg);
            }
        }
    }
}
