using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using PulseRPC.Server.SourceGenerator.Models;

namespace PulseRPC.Server.SourceGenerator.Generators;

/// <summary>
/// 路由表生成器 - 生成高性能的静态路由分发表
/// </summary>
public static class RoutingTableGenerator
{
    /// <summary>
    /// 为所有服务生成统一的路由表
    /// </summary>
    public static string GenerateRoutingTable(List<ServiceModel> services)
    {
        var sb = new StringBuilder();

        // 文件头部
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// This code was generated by PulseRPC.Server.SourceGenerator");
        sb.AppendLine("// High-performance static routing table for all PulseRPC services");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();

        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS8019");
        sb.AppendLine();

        // Using statements
        GenerateUsingStatements(sb, services);

        // 生成全局路由表
        GenerateGlobalRoutingTable(sb, services);

        return sb.ToString();
    }

    /// <summary>
    /// 生成using语句
    /// </summary>
    private static void GenerateUsingStatements(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine("using System.Buffers;");
        sb.AppendLine("using PulseRPC.Abstractions;");
        sb.AppendLine("using PulseRPC.Server.Abstractions;");
        sb.AppendLine("using PulseRPC.Server.Routing;");
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        
        // 添加服务命名空间
        var namespaces = services.Select(s => s.Namespace).Where(ns => !string.IsNullOrEmpty(ns)).Distinct().ToList();
        foreach (var ns in namespaces)
        {
            sb.AppendLine($"using {ns};");
        }
        
        sb.AppendLine();
    }

    /// <summary>
    /// 生成全局路由表类
    /// </summary>
    private static void GenerateGlobalRoutingTable(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("namespace PulseRPC.Generated;");
        sb.AppendLine();

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// High-performance static routing table for all PulseRPC services");
        sb.AppendLine("/// Provides compile-time optimized service dispatch");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public sealed class ServiceRoutingTable : IServiceRoutingTable");
        sb.AppendLine("{");
        sb.AppendLine("    // 单例实例");
        sb.AppendLine("    private static readonly ServiceRoutingTable _instance = new();");
        sb.AppendLine("    ");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 获取路由表单例实例");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static ServiceRoutingTable Instance => _instance;");
        sb.AppendLine("    ");
        sb.AppendLine("    // 私有构造函数，确保单例");
        sb.AppendLine("    private ServiceRoutingTable() { }");
        sb.AppendLine();

        // 生成服务常量
        GenerateServiceConstants(sb, services);

        // 生成路由方法
        GenerateRouteMethod(sb, services);

        // 生成每个服务的专用路由器
        GenerateServiceRouters(sb, services);

        // 生成统计信息
        GenerateStatistics(sb, services);

        // 生成辅助方法
        GenerateHelperMethods(sb, services);

        sb.AppendLine("}");
    }

    /// <summary>
    /// 生成服务名称常量
    /// </summary>
    private static void GenerateServiceConstants(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    #region Service Name Constants");
        sb.AppendLine();

        foreach (var service in services)
        {
            var constantName = GetServiceConstantName(service.InterfaceName);
            sb.AppendLine($"    public const string {constantName} = \"{service.InterfaceFullName}\";");
        }

        sb.AppendLine();

        // 生成预计算的哈希码
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Pre-computed hash codes for fast service name lookup");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static class ServiceHashes");
        sb.AppendLine("    {");

        foreach (var service in services)
        {
            var constantName = GetServiceConstantName(service.InterfaceName);
            var hashCode = (uint)service.InterfaceFullName.GetHashCode();
            sb.AppendLine($"        public const uint {constantName} = {hashCode}u;");
        }

        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    #endregion");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成主路由方法
    /// </summary>
    private static void GenerateRouteMethod(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// High-performance service routing with compile-time optimization");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("    public ValueTask<object?> RouteAsync(IServiceProvider serviceProvider, string serviceName, string methodName, ReadOnlyMemory<byte> data, CancellationToken cancellationToken = default)");
        sb.AppendLine("    {");
        
        // 使用switch表达式进行快速服务分发
        // 支持多种服务名称格式：完整名称、接口名、自定义ServiceName
        sb.AppendLine("        return serviceName switch");
        sb.AppendLine("        {");

        foreach (var service in services)
        {
            var routerMethodName = GetServiceRouterName(service.InterfaceName);

            // 1. 匹配完整接口名称（带命名空间）
            sb.AppendLine($"            \"{service.InterfaceFullName}\" => {routerMethodName}(serviceProvider, methodName, data, cancellationToken),");

            // 2. 匹配接口名称（不带命名空间）
            if (service.InterfaceFullName != service.InterfaceName)
            {
                sb.AppendLine($"            \"{service.InterfaceName}\" => {routerMethodName}(serviceProvider, methodName, data, cancellationToken),");
            }

            // 3. 匹配自定义 ServiceName（如果有）
            if (!string.IsNullOrEmpty(service.ServiceName) &&
                service.ServiceName != service.InterfaceFullName &&
                service.ServiceName != service.InterfaceName)
            {
                sb.AppendLine($"            \"{service.ServiceName}\" => {routerMethodName}(serviceProvider, methodName, data, cancellationToken),");
            }
        }

        sb.AppendLine("            _ => ThrowServiceNotFoundException(serviceName)");
        sb.AppendLine("        };");
        sb.AppendLine("    }");
        sb.AppendLine();

        // 生成哈希码优化版本
        GenerateHashBasedRouting(sb, services);
    }

    /// <summary>
    /// 生成基于哈希码的快速路由
    /// </summary>
    private static void GenerateHashBasedRouting(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Ultra-fast routing using pre-computed hash codes");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("    public ValueTask<object?> RouteByHashAsync(IServiceProvider serviceProvider, uint serviceNameHash, string methodName, ReadOnlyMemory<byte> data, CancellationToken cancellationToken = default)");
        sb.AppendLine("    {");

        sb.AppendLine("        return serviceNameHash switch");
        sb.AppendLine("        {");

        foreach (var service in services)
        {
            var constantName = GetServiceConstantName(service.InterfaceName);
            var routerMethodName = GetServiceRouterName(service.InterfaceName);
            sb.AppendLine($"            ServiceHashes.{constantName} => {routerMethodName}(serviceProvider, methodName, data, cancellationToken),");
        }

        sb.AppendLine("            _ => ThrowServiceNotFoundByHashException(serviceNameHash)");
        sb.AppendLine("        };");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// 为每个服务生成专用路由器
    /// </summary>
    private static void GenerateServiceRouters(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    #region Service-Specific Routers");
        sb.AppendLine();

        foreach (var service in services)
        {
            GenerateServiceRouter(sb, service);
        }

        sb.AppendLine("    #endregion");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成单个服务的路由器
    /// </summary>
    private static void GenerateServiceRouter(StringBuilder sb, ServiceModel service)
    {
        var routerMethodName = GetServiceRouterName(service.InterfaceName);
        var proxyClassName = $"{service.InterfaceName.TrimStart('I')}Proxy";

        sb.AppendLine($"    /// <summary>");
        sb.AppendLine($"    /// Optimized router for {service.InterfaceName}");
        sb.AppendLine($"    /// </summary>");
        sb.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine($"    private ValueTask<object?> {routerMethodName}(IServiceProvider serviceProvider, string methodName, ReadOnlyMemory<byte> data, CancellationToken cancellationToken)");
        sb.AppendLine("    {");

        // 从 ServiceProvider 获取服务实例并创建代理
        sb.AppendLine($"        var proxy = GetOrCreate{service.InterfaceName.TrimStart('I')}Proxy(serviceProvider);");
        sb.AppendLine("        return proxy.InvokeAsync(methodName, data, cancellationToken);");

        sb.AppendLine("    }");
        sb.AppendLine();

        // 生成代理实例获取方法
        GenerateProxyGetter(sb, service);
    }

    /// <summary>
    /// 生成代理实例获取方法
    /// </summary>
    private static void GenerateProxyGetter(StringBuilder sb, ServiceModel service)
    {
        var proxyClassName = $"{service.InterfaceName.TrimStart('I')}Proxy";

        sb.AppendLine($"    /// <summary>");
        sb.AppendLine($"    /// Get or create proxy for {service.InterfaceName} from ServiceProvider");
        sb.AppendLine($"    /// </summary>");
        sb.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine($"    private {service.Namespace}.Generated.{proxyClassName} GetOrCreate{service.InterfaceName.TrimStart('I')}Proxy(IServiceProvider serviceProvider)");
        sb.AppendLine("    {");
        sb.AppendLine($"        var implementation = ({service.InterfaceFullName}?)serviceProvider?.GetService(typeof({service.InterfaceFullName}));");
        sb.AppendLine("        if (implementation is null)");
        sb.AppendLine($"            throw new InvalidOperationException($\"Service implementation for {service.InterfaceName} not registered in DI container\");");
        sb.AppendLine($"        return new {service.Namespace}.Generated.{proxyClassName}(implementation);");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成统计信息
    /// </summary>
    private static void GenerateStatistics(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    #region Statistics and Metadata");
        sb.AppendLine();

        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Routing table statistics and metadata");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static class Statistics");
        sb.AppendLine("    {");
        sb.AppendLine($"        public const int TotalServices = {services.Count};");
        sb.AppendLine($"        public const int TotalMethods = {services.Sum(s => s.Methods.Count)};");
        
        sb.AppendLine("        public static readonly Dictionary<string, int> MethodCountByService = new()");
        sb.AppendLine("        {");
        foreach (var service in services)
        {
            sb.AppendLine($"            [\"{service.InterfaceFullName}\"] = {service.Methods.Count},");
        }
        sb.AppendLine("        };");

        sb.AppendLine();
        sb.AppendLine("        public static readonly string[] AllServiceNames = new[]");
        sb.AppendLine("        {");
        foreach (var service in services)
        {
            sb.AppendLine($"            \"{service.InterfaceFullName}\",  // Full name");
            if (service.InterfaceFullName != service.InterfaceName)
            {
                sb.AppendLine($"            \"{service.InterfaceName}\",  // Short name");
            }
            if (!string.IsNullOrEmpty(service.ServiceName) &&
                service.ServiceName != service.InterfaceFullName &&
                service.ServiceName != service.InterfaceName)
            {
                sb.AppendLine($"            \"{service.ServiceName}\",  // Custom ServiceName");
            }
        }
        sb.AppendLine("        };");

        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    #endregion");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成辅助方法
    /// </summary>
    private static void GenerateHelperMethods(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    #region Helper Methods");
        sb.AppendLine();

        // 异常抛出方法
        sb.AppendLine("    [MethodImpl(MethodImplOptions.NoInlining)]");
        sb.AppendLine("    private static ValueTask<object?> ThrowServiceNotFoundException(string serviceName)");
        sb.AppendLine("    {");
        sb.AppendLine("        throw new ServiceNotFoundException($\"Service '{serviceName}' not found. Available services: {string.Join(\", \", Statistics.AllServiceNames)}\");");
        sb.AppendLine("    }");
        sb.AppendLine();

        sb.AppendLine("    [MethodImpl(MethodImplOptions.NoInlining)]");
        sb.AppendLine("    private static ValueTask<object?> ThrowServiceNotFoundByHashException(uint serviceNameHash)");
        sb.AppendLine("    {");
        sb.AppendLine("        throw new ServiceNotFoundException($\"Service with hash '{serviceNameHash}' not found\");");
        sb.AppendLine("    }");
        sb.AppendLine();

        // 服务提供者访问
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Service provider access (must be set during application startup)");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static IServiceProvider? ServiceProvider { get; set; }");
        sb.AppendLine();

        // 生成 ModuleInitializer
        GenerateModuleInitializer(sb);

        sb.AppendLine("    #endregion");
    }

    /// <summary>
    /// 生成模块初始化器 - 自动注册 ServiceRoutingTable
    /// </summary>
    private static void GenerateModuleInitializer(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Module initializer - automatically registers ServiceRoutingTable when assembly loads");
        sb.AppendLine("    /// This method is called automatically by the runtime before any other code in the assembly");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [System.Runtime.CompilerServices.ModuleInitializer]");
        sb.AppendLine("    internal static void Initialize()");
        sb.AppendLine("    {");
        sb.AppendLine("        // 自动注册路由表实例到全局注册中心");
        sb.AppendLine("        PulseRPC.Server.Routing.ServiceRoutingTableRegistry.Register(Instance);");
        sb.AppendLine("        System.Diagnostics.Debug.WriteLine(\"[PulseRPC] ServiceRoutingTable registered to global registry\");");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// 获取服务常量名称
    /// </summary>
    private static string GetServiceConstantName(string interfaceName)
    {
        return interfaceName.TrimStart('I').ToUpperInvariant() + "_SERVICE";
    }

    /// <summary>
    /// 获取服务路由器方法名
    /// </summary>
    private static string GetServiceRouterName(string interfaceName)
    {
        return $"Route{interfaceName.TrimStart('I')}";
    }

    /// <summary>
    /// 生成SourceText用于编译器集成
    /// </summary>
    public static SourceText GenerateSourceText(List<ServiceModel> services)
    {
        var code = GenerateRoutingTable(services);
        return SourceText.From(code, Encoding.UTF8);
    }
}