/// <summary>
/// 生成响应序列化器注册表及各方法的零拷贝序列化实现。
/// </summary>
public static class ResponseSerializerGenerator
{
    public static SourceText GenerateSourceText(List<ServiceModel> services)
    {
        var code = GenerateCode(services);
        return SourceText.From(code, Encoding.UTF8);
    }

    private static string GenerateCode(List<ServiceModel> services)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// Generated by PulseRPC.Server.SourceGenerator");
        sb.AppendLine("// Response serialization registry - maps service methods to zero-copy serializers");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS0168");
        sb.AppendLine("#pragma warning disable CS0219");
        sb.AppendLine("#pragma warning disable CS8602");
        sb.AppendLine();

        GenerateUsings(sb, services);
        GenerateNamespace(sb, services);

        return sb.ToString();
    }

    private static void GenerateUsings(StringBuilder sb, List<ServiceModel> services)
    {
        // 只保留必要的系统命名空间，不使用自定义类型的命名空间
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Buffers;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using MemoryPack;");
        sb.AppendLine("using PulseRPC.Serialization;");
        // 不再收集和添加自定义类型的命名空间
        sb.AppendLine();
    }

    private static void GenerateNamespace(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("namespace PulseRPC.Generated;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// 自动生成的响应序列化器注册表");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static partial class ResponseSerializers");
        sb.AppendLine("{");

        GenerateRegistry(sb, services);
        sb.AppendLine();
        GenerateSerializerContainer(sb, services);

        GenerateModuleInitializer(sb);

        sb.AppendLine("}");
    }

    private static void GenerateRegistry(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    private sealed class RegistryImpl : IResponseSerializerRegistry");
        sb.AppendLine("    {");
        sb.AppendLine("        private static readonly IResponseSerializer[] s_serializers = new IResponseSerializer[]");
        sb.AppendLine("        {");

        var serializerInfos = CollectSerializerInfos(services);
        for (var i = 0; i < serializerInfos.Count; i++)
        {
            var info = serializerInfos[i];
            var comma = i == serializerInfos.Count - 1 ? string.Empty : ",";
            sb.AppendLine($"            Serializers.{info.ClassName}.Instance{comma}");
        }

        sb.AppendLine("        };");
        sb.AppendLine();

        sb.AppendLine("        private static readonly Dictionary<string, Dictionary<string, IResponseSerializer>> s_map = BuildMap();");
        sb.AppendLine("        private static readonly Dictionary<ushort, IResponseSerializer> s_protocolMap = BuildProtocolMap();");
        sb.AppendLine();
        sb.AppendLine("        private static Dictionary<string, Dictionary<string, IResponseSerializer>> BuildMap()");
        sb.AppendLine("        {");
        sb.AppendLine("            var services = new Dictionary<string, Dictionary<string, IResponseSerializer>>(StringComparer.Ordinal);");

        foreach (var service in services)
        {
            sb.AppendLine($"            var methods_{GetSafeIdentifier(service.InterfaceName)} = new Dictionary<string, IResponseSerializer>(StringComparer.Ordinal)");
            sb.AppendLine("            {");
            var methods = service.Methods;
            var includedMethods = methods.Select(m => serializerInfos.FirstOrDefault(si => si.ServiceName == service.ServiceName && si.MethodName == m.MethodName)).Where(si => si != null).ToList();
            for (var i = 0; i < includedMethods.Count; i++)
            {
                var info = includedMethods[i]!;
                var comma = i == includedMethods.Count - 1 ? string.Empty : ",";
                sb.AppendLine($"                [\"{info.MethodName}\"] = Serializers.{info.ClassName}.Instance{comma}");
            }
            sb.AppendLine("            };");
            sb.AppendLine($"            services[\"{service.ServiceName}\"] = methods_{GetSafeIdentifier(service.InterfaceName)};");

            if (service.ServiceName != service.InterfaceFullName)
            {
                sb.AppendLine($"            services[\"{service.InterfaceFullName}\"] = methods_{GetSafeIdentifier(service.InterfaceName)};");
            }
        }

        sb.AppendLine("            return services;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private static Dictionary<ushort, IResponseSerializer> BuildProtocolMap()");
        sb.AppendLine("        {");
        sb.AppendLine("            var protocols = new Dictionary<ushort, IResponseSerializer>();");
        sb.AppendLine();

        foreach (var info in serializerInfos)
        {
            sb.AppendLine($"            protocols[0x{info.ProtocolId:X4}] = Serializers.{info.ClassName}.Instance;");
        }

        sb.AppendLine();
        sb.AppendLine("            return protocols;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public bool TryGetSerializer(ushort protocolId, [NotNullWhen(true)] out IResponseSerializer? serializer)");
        sb.AppendLine("        {");
        sb.AppendLine("            return s_protocolMap.TryGetValue(protocolId, out serializer);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public bool TryGetSerializer(string serviceName, string methodName, [NotNullWhen(true)] out IResponseSerializer? serializer)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (s_map.TryGetValue(serviceName, out var methods) && methods.TryGetValue(methodName, out serializer))");
        sb.AppendLine("            {");
        sb.AppendLine("                return true;");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            serializer = default!;");
        sb.AppendLine("            return false;");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        public ReadOnlySpan<IResponseSerializer> EnumerateSerializers() => s_serializers;");
        sb.AppendLine("    }");
    }

    private static void GenerateSerializerContainer(StringBuilder sb, List<ServiceModel> services)
    {
        var serializerInfos1 = CollectSerializerInfos(services);
        if (serializerInfos1.Count == 0)
        {
            return;
        }

        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 自动生成的响应序列化器实现。");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static class Serializers");
        sb.AppendLine("    {");

        foreach (var info in serializerInfos1)
        {
            GenerateSerializerClass(sb, info);
            sb.AppendLine();
        }

        sb.AppendLine("    }");
    }

    private static void GenerateSerializerClass(StringBuilder sb, ResponseSerializerInfo info)
    {
        sb.AppendLine($"        public sealed class {info.ClassName} : IResponseSerializer");
        sb.AppendLine("        {");
        sb.AppendLine($"            public static {info.ClassName} Instance {{ get; }} = new();");
        sb.AppendLine();
        sb.AppendLine($"            private {info.ClassName}() {{ }}");
        sb.AppendLine();
        sb.AppendLine($"            public string ServiceName => \"{info.ServiceName}\";");
        sb.AppendLine($"            public string MethodName => \"{info.MethodName}\";");
        sb.AppendLine($"            public ushort ProtocolId => 0x{info.ProtocolId:X4}; // {info.ProtocolId}");
        sb.AppendLine();

        sb.AppendLine($"            // A {info.ResponseTypeName}");

        if (! string.IsNullOrWhiteSpace(info.ResponseTypeName))
        {
            sb.AppendLine("            public void Serialize(object response, IBufferWriter<byte> writer)");
            sb.AppendLine("            {");
            sb.AppendLine("                if (response is null)");
            sb.AppendLine("                {");
            sb.AppendLine("                    throw new ArgumentNullException(nameof(response));");
            sb.AppendLine("                }");
            sb.AppendLine();

            // 检测是否为元组类型，元组类型不能使用模式匹配
            if (IsTupleType(info.ResponseTypeName))
            {
                // 对于元组类型，使用类型检查和转换
                sb.AppendLine($"                if (response.GetType() == typeof({info.ResponseTypeName}))");
                sb.AppendLine("                {");
                sb.AppendLine($"                    var typed = ({info.ResponseTypeName})response;");
                sb.AppendLine("                    MemoryPackSerializer.Serialize(writer, typed);");
                sb.AppendLine("                    return;");
                sb.AppendLine("                }");
            }
            else
            {
                // 对于非元组类型，使用模式匹配
                sb.AppendLine($"                if (response is {info.ResponseTypeName} typed)");
                sb.AppendLine("                {");
                sb.AppendLine("                    MemoryPackSerializer.Serialize(writer, typed);");
                sb.AppendLine("                    return;");
                sb.AppendLine("                }");
            }

            sb.AppendLine();
            sb.AppendLine($"                throw new InvalidCastException($\"期待类型 {{typeof({info.ResponseTypeName})}} 但接收到 {{response.GetType()}}\");");
            sb.AppendLine("            }");
            sb.AppendLine();

            sb.AppendLine("            public ValueTask SerializeAsync(object response, IBufferWriter<byte> writer, CancellationToken cancellationToken = default)");
            sb.AppendLine("            {");
            sb.AppendLine("                Serialize(response, writer);");
            sb.AppendLine("                return ValueTask.CompletedTask;");
            sb.AppendLine("            }");
            sb.AppendLine();

            sb.AppendLine("            public bool TryGetTypedSerializer<T>(out Action<T, IBufferWriter<byte>> serializer)");
            sb.AppendLine("            {");
            sb.AppendLine($"                if (typeof(T) == typeof({info.ResponseTypeName}))");
            sb.AppendLine("                {");
            sb.AppendLine("                    serializer = static (value, bufferWriter) =>");
            sb.AppendLine("                    {");
            sb.AppendLine($"                        MemoryPackSerializer.Serialize(bufferWriter, Unsafe.As<T, {info.ResponseTypeName}>(ref value));");
            sb.AppendLine("                    };");
            sb.AppendLine("                    return true;");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine("                serializer = null!;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
        }
        else
        {
            sb.AppendLine("            public void Serialize(object response, IBufferWriter<byte> writer)");
            sb.AppendLine("            {");
            sb.AppendLine("                if (response is null || response is EmptyResponse)");
            sb.AppendLine("                {");
            sb.AppendLine("                    return;");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine($"                throw new InvalidOperationException($\"方法 {{ServiceName}}.{{MethodName}} 不期望返回值\");");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            public ValueTask SerializeAsync(object response, IBufferWriter<byte> writer, CancellationToken cancellationToken = default)");
            sb.AppendLine("            {");
            sb.AppendLine("                Serialize(response, writer);");
            sb.AppendLine("                return ValueTask.CompletedTask;");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            public bool TryGetTypedSerializer<T>(out Action<T, IBufferWriter<byte>> serializer)");
            sb.AppendLine("            {");
            sb.AppendLine("                serializer = null!;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
        }

        sb.AppendLine("        }");
    }

    private static IReadOnlyList<ResponseSerializerInfo> CollectSerializerInfos(List<ServiceModel> services)
    {
        var list = new List<ResponseSerializerInfo>();

        foreach (var service in services)
        {
            foreach (var method in service.Methods)
            {
                // 跳过非 MemoryPackable 类型（让它们使用反射路径）
                // 但为 void 返回和 MemoryPackable 类型生成序列化器
                if (!string.IsNullOrWhiteSpace(method.ResponseTypeFullName) && !method.IsResponseMemoryPackable)
                {
                    // 有返回值但不是 MemoryPackable，跳过生成，使用反射降级路径
                    continue;
                }

                var className = $"{GetSafeIdentifier(service.ServiceName ?? service.InterfaceName)}_{GetSafeIdentifier(method.MethodName)}_ResponseSerializer";

                // responseType 为 null 表示 void 返回，否则为 MemoryPackable 类型
                var responseType = string.IsNullOrWhiteSpace(method.ResponseTypeFullName) ? null : method.ResponseTypeFullName;

                // 移除末尾的可空标记
                if (!string.IsNullOrEmpty(responseType) && responseType.EndsWith('?'))
                {
                    responseType = responseType[..^1];
                }

                // 对于元组类型，移除元素名称（如 guild, member 等）
                // 例如：(GuildEntity? guild, GuildMember? member) -> (GuildEntity?, GuildMember?)
                if (!string.IsNullOrEmpty(responseType) && IsTupleType(responseType))
                {
                    responseType = StripTupleElementNames(responseType);
                }

                list.Add(new ResponseSerializerInfo(
                    className,
                    service.ServiceName ?? service.InterfaceName,
                    method.MethodName,
                    method.ProtocolId,
                    responseType));
            }
        }

        return list;
    }

    private static IEnumerable<string> CollectNamespaces(List<ServiceModel> services)
    {
        var set = new HashSet<string>();

        foreach (var service in services)
        {
            if (!string.IsNullOrWhiteSpace(service.Namespace) && IsValidNamespace(service.Namespace))
            {
                set.Add(service.Namespace);
            }

            foreach (var method in service.Methods)
            {
                if (!string.IsNullOrWhiteSpace(method.ResponseTypeFullName))
                {
                    ExtractNamespacesFromType(method.ResponseTypeFullName!, set);
                }
            }
        }

        // Filter out any invalid namespaces and return sorted
        return set.Where(IsValidNamespace).OrderBy(n => n);
    }

    /// <summary>
    /// Validates that a namespace string is a valid C# namespace identifier
    /// </summary>
    private static bool IsValidNamespace(string ns)
    {
        if (string.IsNullOrWhiteSpace(ns))
            return false;

        // Namespace should not contain generic markers, special chars, or be a keyword
        if (ns.Contains('<') || ns.Contains('>') || ns.Contains('?') ||
            ns.Contains('[') || ns.Contains(']') || ns.Contains(' ') ||
            ns.Contains('@') || ns.Contains('`'))
            return false;

        // Each part of the namespace should be a valid identifier
        var parts = ns.Split('.');
        foreach (var part in parts)
        {
            if (string.IsNullOrEmpty(part))
                return false;

            // First character must be letter or underscore
            if (!char.IsLetter(part[0]) && part[0] != '_')
                return false;

            // Remaining characters must be letters, digits, or underscores
            for (var i = 1; i < part.Length; i++)
            {
                if (!char.IsLetterOrDigit(part[i]) && part[i] != '_')
                    return false;
            }
        }

        return true;
    }

    /// <summary>
    /// 从类型全名中提取所有命名空间（包括泛型参数和元组元素）
    /// </summary>
    private static void ExtractNamespacesFromType(string typeFullName, HashSet<string> namespaces)
    {
        if (string.IsNullOrWhiteSpace(typeFullName))
            return;

        var trimmed = typeFullName.Trim();

        // 移除可能的可空标记
        if (trimmed.EndsWith('?'))
        {
            trimmed = trimmed[..^1].Trim();
        }

        // 移除参数名（如果类型名包含参数名，如 "Guild? guild" -> "Guild?"）
        // 查找最后一个空格，如果后面跟着有效的标识符，则可能是参数名
        // 但要注意：空格可能也在类型名内部（如泛型参数中的空格），所以需要更智能的判断
        trimmed = RemoveParameterName(trimmed);

        // 检查是否是元组类型
        if (trimmed.StartsWith('(') && trimmed.EndsWith(')'))
        {
            // 提取元组元素（移除外层的括号）
            var tupleContent = trimmed.Substring(1, trimmed.Length - 2).Trim();
            var elements = SplitTupleElements(tupleContent);
            foreach (var element in elements)
            {
                var elementTrimmed = element.Trim();
                if (!string.IsNullOrWhiteSpace(elementTrimmed))
                {
                    // 递归处理每个元组元素
                    ExtractNamespacesFromType(elementTrimmed, namespaces);
                }
            }
            return;
        }

        // 检查是否是泛型类型
        var genericStart = trimmed.IndexOf('<');
        if (genericStart > 0)
        {
            // 提取泛型定义的命名空间（如 System.Collections.Generic.List<T> 中的 System.Collections.Generic）
            var genericDefinition = trimmed[..genericStart];
            var lastDot = genericDefinition.LastIndexOf('.');
            if (lastDot > 0)
            {
                var ns = genericDefinition[..lastDot];
                if (!string.IsNullOrWhiteSpace(ns) && IsValidNamespace(ns))
                {
                    namespaces.Add(ns);
                }
            }

            // 提取泛型参数的命名空间
            var genericEnd = trimmed.LastIndexOf('>');
            if (genericEnd > genericStart)
            {
                var genericArgs = trimmed.Substring(genericStart + 1, genericEnd - genericStart - 1);

                // 处理多个泛型参数（用逗号分隔）
                var args = SplitGenericArguments(genericArgs);
                foreach (var arg in args)
                {
                    var trimmedArg = arg.Trim();
                    if (!string.IsNullOrWhiteSpace(trimmedArg))
                    {
                        // 递归处理嵌套泛型
                        ExtractNamespacesFromType(trimmedArg, namespaces);
                    }
                }
            }
        }
        else
        {
            // 非泛型类型，直接提取命名空间
            var lastDot = trimmed.LastIndexOf('.');
            if (lastDot > 0)
            {
                var ns = trimmed[..lastDot];
                if (!string.IsNullOrWhiteSpace(ns) && IsValidNamespace(ns))
                {
                    namespaces.Add(ns);
                }
            }
        }
    }

    /// <summary>
    /// 移除类型名中的参数名
    /// 例如："Libra.Modules.GuildSystem.Guild? guild" -> "Libra.Modules.GuildSystem.Guild?"
    /// 例如："int count" -> "int"
    /// </summary>
    private static string RemoveParameterName(string typeName)
    {
        if (string.IsNullOrWhiteSpace(typeName))
            return typeName;

        var trimmed = typeName.Trim();

        // 如果类型名包含泛型或元组，参数名应该在最后（在 > 或 ) 之后）
        // 例如："List<int> items" -> "List<int>"
        var genericEnd = trimmed.LastIndexOf('>');
        var tupleEnd = trimmed.LastIndexOf(')');
        var lastBracket = Math.Max(genericEnd, tupleEnd);

        if (lastBracket >= 0)
        {
            // 有泛型或元组，参数名应该在括号之后
            var afterBracket = trimmed.Substring(lastBracket + 1).Trim();
            if (!string.IsNullOrWhiteSpace(afterBracket))
            {
                // 检查括号后是否有空格和标识符（参数名）
                var spaceIndex = afterBracket.IndexOf(' ');
                if (spaceIndex > 0 && spaceIndex < afterBracket.Length - 1)
                {
                    var afterSpace = afterBracket.Substring(spaceIndex + 1).Trim();
                    if (IsValidIdentifier(afterSpace))
                    {
                        // 移除参数名
                        return trimmed.Substring(0, lastBracket + 1 + spaceIndex).Trim();
                    }
                }
            }
        }
        else
        {
            // 没有泛型或元组，查找最后一个空格
            var lastSpace = trimmed.LastIndexOf(' ');
            if (lastSpace > 0 && lastSpace < trimmed.Length - 1)
            {
                var afterSpace = trimmed.Substring(lastSpace + 1).Trim();
                // 如果空格后是有效的 C# 标识符（可能是参数名），则移除它
                if (IsValidIdentifier(afterSpace))
                {
                    // 但需要确保这不是类型名的一部分（如 "System.Collections.Generic"）
                    // 检查空格前的部分是否以点结尾，如果是，则可能是命名空间的一部分
                    var beforeSpace = trimmed.Substring(0, lastSpace).Trim();
                    if (!beforeSpace.EndsWith('.'))
                    {
                        return beforeSpace;
                    }
                }
            }
        }

        return trimmed;
    }

    /// <summary>
    /// 分割泛型参数，正确处理嵌套泛型
    /// 例如：Dictionary&lt;string, List&lt;int&gt;&gt; 应该分割为 ["string", "List&lt;int&gt;"]
    /// </summary>
    private static List<string> SplitGenericArguments(string genericArgs)
    {
        var result = new List<string>();
        var current = new StringBuilder();
        var depth = 0;

        foreach (var ch in genericArgs)
        {
            if (ch == '<')
            {
                depth++;
                current.Append(ch);
            }
            else if (ch == '>')
            {
                depth--;
                current.Append(ch);
            }
            else if (ch == ',' && depth == 0)
            {
                // 顶层逗号，分隔参数
                result.Add(current.ToString());
                current.Clear();
            }
            else
            {
                current.Append(ch);
            }
        }

        if (current.Length > 0)
        {
            result.Add(current.ToString());
        }

        return result;
    }

    /// <summary>
    /// 分割元组元素，正确处理嵌套元组
    /// 例如：(string, int) 应该分割为 ["string", "int"]
    /// 例如：((string, int), bool) 应该分割为 ["(string, int)", "bool"]
    /// </summary>
    private static List<string> SplitTupleElements(string tupleContent)
    {
        var result = new List<string>();
        var current = new StringBuilder();
        var depth = 0;

        foreach (var ch in tupleContent)
        {
            if (ch == '(')
            {
                depth++;
                current.Append(ch);
            }
            else if (ch == ')')
            {
                depth--;
                current.Append(ch);
            }
            else if (ch == ',' && depth == 0)
            {
                // 顶层逗号，分隔元素
                result.Add(current.ToString());
                current.Clear();
            }
            else
            {
                current.Append(ch);
            }
        }

        if (current.Length > 0)
        {
            result.Add(current.ToString());
        }

        return result;
    }

    /// <summary>
    /// 检查字符串是否是有效的 C# 标识符（用于检测参数名）
    /// </summary>
    private static bool IsValidIdentifier(string text)
    {
        if (string.IsNullOrWhiteSpace(text))
            return false;

        var trimmed = text.Trim();
        if (trimmed.Length == 0)
            return false;

        // 第一个字符必须是字母、下划线或 @
        if (!char.IsLetter(trimmed[0]) && trimmed[0] != '_' && trimmed[0] != '@')
            return false;

        // 其余字符必须是字母、数字或下划线
        for (int i = 1; i < trimmed.Length; i++)
        {
            if (!char.IsLetterOrDigit(trimmed[i]) && trimmed[i] != '_')
                return false;
        }

        return true;
    }

    /// <summary>
    /// 生成模块初始化器 - 自动注册 ResponseSerializer
    /// </summary>
