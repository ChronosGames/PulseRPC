using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis.Text;
using PulseRPC.Server.SourceGenerator.Models;

namespace PulseRPC.Server.SourceGenerator.Generators;

/// <summary>
/// 生成响应序列化器注册表及各方法的零拷贝序列化实现。
/// </summary>
public static class ResponseSerializerGenerator
{
    public static SourceText GenerateSourceText(List<ServiceModel> services)
    {
        var code = GenerateCode(services);
        return SourceText.From(code, Encoding.UTF8);
    }

    private static string GenerateCode(List<ServiceModel> services)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// Generated by PulseRPC.Server.SourceGenerator");
        sb.AppendLine("// Response serialization registry - maps service methods to zero-copy serializers");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS0168");
        sb.AppendLine("#pragma warning disable CS0219");
        sb.AppendLine("#pragma warning disable CS8602");
        sb.AppendLine();

        GenerateUsings(sb, services);
        GenerateNamespace(sb, services);

        return sb.ToString();
    }

    private static void GenerateUsings(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Buffers;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using MemoryPack;");
        sb.AppendLine("using PulseRPC.Abstractions;");
        sb.AppendLine("using PulseRPC.Serialization;");

        foreach (var ns in CollectNamespaces(services))
        {
            sb.AppendLine($"using {ns};");
        }

        sb.AppendLine();
    }

    private static void GenerateNamespace(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("namespace PulseRPC.Generated;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// 自动生成的响应序列化器注册表");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static partial class GeneratedResponseSerializers");
        sb.AppendLine("{");

        GenerateRegistry(sb, services);
        sb.AppendLine();
        GenerateSerializerContainer(sb, services);

        sb.AppendLine("}");
    }

    private static void GenerateRegistry(StringBuilder sb, List<ServiceModel> services)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 运行时可用的响应序列化器注册表实例。");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static readonly IResponseSerializerRegistry Registry = new RegistryImpl();");
        sb.AppendLine();

        sb.AppendLine("    private sealed class RegistryImpl : IResponseSerializerRegistry");
        sb.AppendLine("    {");
        sb.AppendLine("        private static readonly IResponseSerializer[] s_serializers = new IResponseSerializer[]");
        sb.AppendLine("        {");

        var serializerInfos = CollectSerializerInfos(services);
        for (var i = 0; i < serializerInfos.Count; i++)
        {
            var info = serializerInfos[i];
            var comma = i == serializerInfos.Count - 1 ? string.Empty : ",";
            sb.AppendLine($"            Serializers.{info.ClassName}.Instance{comma}");
        }

        sb.AppendLine("        };");
        sb.AppendLine();

        sb.AppendLine("        private static readonly Dictionary<string, Dictionary<string, IResponseSerializer>> s_map = BuildMap();");
        sb.AppendLine();
        sb.AppendLine("        private static Dictionary<string, Dictionary<string, IResponseSerializer>> BuildMap()");
        sb.AppendLine("        {");
        sb.AppendLine("            var services = new Dictionary<string, Dictionary<string, IResponseSerializer>>(StringComparer.Ordinal);");

        foreach (var service in services)
        {
            sb.AppendLine($"            var methods_{GetSafeIdentifier(service.InterfaceName)} = new Dictionary<string, IResponseSerializer>(StringComparer.Ordinal)");
            sb.AppendLine("            {");
            var methods = service.Methods;
            var includedMethods = methods.Select(m => serializerInfos.FirstOrDefault(si => si.ServiceName == service.ServiceName && si.MethodName == m.MethodName)).Where(si => si != null).ToList();
            for (var i = 0; i < includedMethods.Count; i++)
            {
                var info = includedMethods[i]!;
                var comma = i == includedMethods.Count - 1 ? string.Empty : ",";
                sb.AppendLine($"                [\"{info.MethodName}\"] = Serializers.{info.ClassName}.Instance{comma}");
            }
            sb.AppendLine("            };");
            sb.AppendLine($"            services[\"{service.ServiceName}\"] = methods_{GetSafeIdentifier(service.InterfaceName)};");

            if (service.ServiceName != service.InterfaceFullName)
            {
                sb.AppendLine($"            services[\"{service.InterfaceFullName}\"] = methods_{GetSafeIdentifier(service.InterfaceName)};");
            }
        }

        sb.AppendLine("            return services;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public bool TryGetSerializer(string serviceName, string methodName, [NotNullWhen(true)] out IResponseSerializer? serializer)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (s_map.TryGetValue(serviceName, out var methods) && methods.TryGetValue(methodName, out serializer))");
        sb.AppendLine("            {");
        sb.AppendLine("                return true;");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            serializer = default!;");
        sb.AppendLine("            return false;");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        public ReadOnlySpan<IResponseSerializer> EnumerateSerializers() => s_serializers;");
        sb.AppendLine("    }");
    }

    private static void GenerateSerializerContainer(StringBuilder sb, List<ServiceModel> services)
    {
        var serializerInfos = CollectSerializerInfos(services);
        if (serializerInfos.Count == 0)
        {
            return;
        }

        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 自动生成的响应序列化器实现。");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static class Serializers");
        sb.AppendLine("    {");

        foreach (var info in serializerInfos)
        {
            GenerateSerializerClass(sb, info);
            sb.AppendLine();
        }

        sb.AppendLine("    }");
    }

    private static void GenerateSerializerClass(StringBuilder sb, ResponseSerializerInfo info)
    {
        sb.AppendLine($"        public sealed class {info.ClassName} : IResponseSerializer");
        sb.AppendLine("        {");
        sb.AppendLine($"            public static {info.ClassName} Instance {{ get; }} = new();");
        sb.AppendLine();
        sb.AppendLine($"            private {info.ClassName}() {{ }}");
        sb.AppendLine();
        sb.AppendLine($"            public string ServiceName => \"{info.ServiceName}\";");
        sb.AppendLine($"            public string MethodName => \"{info.MethodName}\";");
        sb.AppendLine();

        if (info.ResponseTypeName != null)
        {
            sb.AppendLine("            public void Serialize(object response, IBufferWriter<byte> writer)");
            sb.AppendLine("            {");
            sb.AppendLine("                if (response is null)");
            sb.AppendLine("                {");
            sb.AppendLine("                    throw new ArgumentNullException(nameof(response));");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine($"                if (response is {info.ResponseTypeName} typed)");
            sb.AppendLine("                {");
            sb.AppendLine("                    MemoryPackSerializer.Serialize(writer, typed);");
            sb.AppendLine("                    return;");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine($"                throw new InvalidCastException($\"期待类型 {{typeof({info.ResponseTypeName})}} 但接收到 {{response.GetType()}}\");");
            sb.AppendLine("            }");
            sb.AppendLine();

            sb.AppendLine("            public ValueTask SerializeAsync(object response, IBufferWriter<byte> writer, CancellationToken cancellationToken = default)");
            sb.AppendLine("            {");
            sb.AppendLine("                Serialize(response, writer);");
            sb.AppendLine("                return ValueTask.CompletedTask;");
            sb.AppendLine("            }");
            sb.AppendLine();

            sb.AppendLine("            public bool TryGetTypedSerializer<T>(out Action<T, IBufferWriter<byte>> serializer)");
            sb.AppendLine("            {");
            sb.AppendLine($"                if (typeof(T) == typeof({info.ResponseTypeName}))");
            sb.AppendLine("                {");
            sb.AppendLine("                    serializer = static (value, bufferWriter) =>");
            sb.AppendLine("                    {");
            sb.AppendLine($"                        MemoryPackSerializer.Serialize(bufferWriter, Unsafe.As<T, {info.ResponseTypeName}>(ref value));");
            sb.AppendLine("                    };");
            sb.AppendLine("                    return true;");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine("                serializer = null!;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
        }
        else
        {
            sb.AppendLine("            public void Serialize(object response, IBufferWriter<byte> writer)");
            sb.AppendLine("            {");
            sb.AppendLine("                if (response is null || response is EmptyResponse)");
            sb.AppendLine("                {");
            sb.AppendLine("                    return;");
            sb.AppendLine("                }");
            sb.AppendLine();
            sb.AppendLine($"                throw new InvalidOperationException($\"方法 {{ServiceName}}.{{MethodName}} 不期望返回值\");");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            public ValueTask SerializeAsync(object response, IBufferWriter<byte> writer, CancellationToken cancellationToken = default)");
            sb.AppendLine("            {");
            sb.AppendLine("                Serialize(response, writer);");
            sb.AppendLine("                return ValueTask.CompletedTask;");
            sb.AppendLine("            }");
            sb.AppendLine();
            sb.AppendLine("            public bool TryGetTypedSerializer<T>(out Action<T, IBufferWriter<byte>> serializer)");
            sb.AppendLine("            {");
            sb.AppendLine("                serializer = null!;");
            sb.AppendLine("                return false;");
            sb.AppendLine("            }");
        }

        sb.AppendLine("        }");
    }

    private static IReadOnlyList<ResponseSerializerInfo> CollectSerializerInfos(List<ServiceModel> services)
    {
        var list = new List<ResponseSerializerInfo>();

        foreach (var service in services)
        {
            foreach (var method in service.Methods)
            {
                var className = $"{GetSafeIdentifier(service.ServiceName ?? service.InterfaceName)}_{GetSafeIdentifier(method.MethodName)}_ResponseSerializer";
                var responseType = string.IsNullOrWhiteSpace(method.ResponseTypeFullName) ? null : method.ResponseTypeFullName;

                list.Add(new ResponseSerializerInfo(
                    className,
                    service.ServiceName ?? service.InterfaceName,
                    method.MethodName,
                    responseType));
            }
        }

        return list;
    }

    private static IEnumerable<string> CollectNamespaces(List<ServiceModel> services)
    {
        var set = new HashSet<string>();

        foreach (var service in services)
        {
            if (!string.IsNullOrWhiteSpace(service.Namespace))
            {
                set.Add(service.Namespace);
            }

            foreach (var method in service.Methods)
            {
                if (!string.IsNullOrWhiteSpace(method.ResponseTypeFullName))
                {
                    var lastDot = method.ResponseTypeFullName!.LastIndexOf('.');
                    if (lastDot > 0)
                    {
                        var ns = method.ResponseTypeFullName.Substring(0, lastDot);
                        if (!string.IsNullOrWhiteSpace(ns))
                        {
                            set.Add(ns);
                        }
                    }
                }
            }
        }

        return set.OrderBy(n => n);
    }

    private static string GetSafeIdentifier(string value)
    {
        var sb = new StringBuilder(value.Length);
        foreach (var ch in value)
        {
            sb.Append(char.IsLetterOrDigit(ch) ? ch : '_');
        }
        return sb.ToString();
    }

    private sealed class ResponseSerializerInfo
    {
        public string ClassName { get; }
        public string ServiceName { get; }
        public string MethodName { get; }
        public string? ResponseTypeName { get; }

        public ResponseSerializerInfo(string className, string serviceName, string methodName, string? responseTypeName)
        {
            ClassName = className;
            ServiceName = serviceName;
            MethodName = methodName;
            ResponseTypeName = responseTypeName;
        }
    }
}

