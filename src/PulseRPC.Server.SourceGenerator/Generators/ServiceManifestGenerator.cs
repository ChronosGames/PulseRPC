using System.Collections.Generic;
using System.Text;
using Microsoft.CodeAnalysis.Text;
using PulseRPC.Server.SourceGenerator.Models;

namespace PulseRPC.Server.SourceGenerator.Generators;

/// <summary>
/// 生成服务元数据清单。
/// </summary>
public static class ServiceManifestGenerator
{
    public static SourceText GenerateSourceText(List<ServiceModel> services)
    {
        var code = GenerateCode(services);
        return SourceText.From(code, Encoding.UTF8);
    }

    private static string GenerateCode(List<ServiceModel> services)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// Generated by PulseRPC.Server.SourceGenerator");
        sb.AppendLine("// Service metadata manifest for management and diagnostics");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using PulseRPC.Server;");
        sb.AppendLine();
        sb.AppendLine("namespace PulseRPC.Generated;");
        sb.AppendLine();
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// 自动生成的服务元数据清单。");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static partial class ServiceManifest");
        sb.AppendLine("{");
        sb.AppendLine("    private sealed class ManifestImpl : IServiceManifest");
        sb.AppendLine("    {");
        sb.AppendLine("        private static readonly ServiceInfo[] s_services = new ServiceInfo[]");
        sb.AppendLine("        {");

        for (var i = 0; i < services.Count; i++)
        {
            GenerateServiceInfo(sb, services[i], i == services.Count - 1);
        }

        sb.AppendLine("        };");
        sb.AppendLine();
        sb.AppendLine("        public IReadOnlyList<ServiceInfo> Services => s_services;");
        sb.AppendLine("    }");
        sb.AppendLine();
        sb.AppendLine("    public static IServiceManifest Instance { get; } = new ManifestImpl();");
        sb.AppendLine();
        sb.AppendLine("    [System.Runtime.CompilerServices.ModuleInitializer]");
        sb.AppendLine("    internal static void Initialize()");
        sb.AppendLine("    {");
        sb.AppendLine("        PulseRPC.Server.ServiceManifestRegistry.Register(Instance);");
        sb.AppendLine("        System.Diagnostics.Debug.WriteLine(\"[PulseRPC] ServiceManifest registered to global registry\");");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void GenerateServiceInfo(StringBuilder sb, ServiceModel service, bool isLastService)
    {
        var comma = isLastService ? string.Empty : ",";
        var serviceName = service.ServiceName ?? service.InterfaceName;

        sb.AppendLine("            new ServiceInfo");
        sb.AppendLine("            {");
        sb.AppendLine($"                ServiceName = \"{EscapeString(serviceName)}\",");
        sb.AppendLine($"                HubType = typeof({ToTypeReference(service.InterfaceFullName)}),");
        sb.AppendLine($"                ChannelName = \"{EscapeString(service.ChannelName)}\",");
        sb.AppendLine("                Methods = new ServiceMethodInfo[]");
        sb.AppendLine("                {");

        for (var i = 0; i < service.Methods.Count; i++)
        {
            GenerateMethodInfo(sb, service.Methods[i], i == service.Methods.Count - 1);
        }

        sb.AppendLine("                }");
        sb.AppendLine($"            }}{comma}");
    }

    private static void GenerateMethodInfo(StringBuilder sb, MethodModel method, bool isLastMethod)
    {
        var comma = isLastMethod ? string.Empty : ",";
        var declaringHubTypeName = method.DeclaringInterfaceFullName ?? string.Empty;

        sb.AppendLine("                    new ServiceMethodInfo");
        sb.AppendLine("                    {");
        sb.AppendLine($"                        MethodName = \"{EscapeString(method.MethodName)}\",");
        sb.AppendLine($"                        DeclaringHubTypeName = \"{EscapeString(NormalizeTypeName(declaringHubTypeName))}\",");
        sb.AppendLine($"                        ProtocolId = 0x{method.ProtocolId:X4},");
        sb.AppendLine($"                        ReturnTypeName = \"{EscapeString(NormalizeTypeName(method.ReturnTypeFullName))}\",");
        sb.AppendLine($"                        ResponseTypeName = {FormatNullableString(NormalizeTypeName(method.ResponseTypeFullName))},");
        sb.AppendLine($"                        IsAsync = {FormatBoolean(method.IsAsync)},");
        sb.AppendLine($"                        IsClientFacing = {FormatBoolean(method.IsClientFacing)},");
        sb.AppendLine($"                        IsReentrant = {FormatBoolean(method.IsReentrant)},");
        sb.AppendLine("                        Parameters = new ServiceParameterInfo[]");
        sb.AppendLine("                        {");

        for (var i = 0; i < method.Parameters.Count; i++)
        {
            GenerateParameterInfo(sb, method.Parameters[i], i == method.Parameters.Count - 1);
        }

        sb.AppendLine("                        }");
        sb.AppendLine($"                    }}{comma}");
    }

    private static void GenerateParameterInfo(StringBuilder sb, ParameterModel parameter, bool isLastParameter)
    {
        var comma = isLastParameter ? string.Empty : ",";
        sb.AppendLine("                            new ServiceParameterInfo");
        sb.AppendLine("                            {");
        sb.AppendLine($"                                Name = \"{EscapeString(parameter.Name)}\",");
        sb.AppendLine($"                                TypeName = \"{EscapeString(NormalizeTypeName(parameter.TypeFullName))}\"");
        sb.AppendLine($"                            }}{comma}");
    }

    private static string ToTypeReference(string typeName)
    {
        var normalized = NormalizeTypeName(typeName);
        return normalized.StartsWith("global::", System.StringComparison.Ordinal)
            ? normalized
            : "global::" + normalized;
    }

    private static string NormalizeTypeName(string? typeName)
    {
        var value = typeName;
        if (string.IsNullOrWhiteSpace(value))
        {
            return string.Empty;
        }

        return value!.Replace("global::", string.Empty);
    }

    private static string FormatNullableString(string value)
    {
        return string.IsNullOrWhiteSpace(value)
            ? "null"
            : $"\"{EscapeString(value)}\"";
    }

    private static string FormatBoolean(bool value) => value ? "true" : "false";

    private static string EscapeString(string value)
    {
        return value
            .Replace("\\", "\\\\")
            .Replace("\"", "\\\"");
    }
}
