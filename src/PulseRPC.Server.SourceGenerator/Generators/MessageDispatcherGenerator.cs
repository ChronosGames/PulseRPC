/// </summary>
public static class MessageDispatcherGenerator
{
    /// <summary>
    /// 生成编译时消息分发器代码
    /// </summary>
    public static string GenerateSourceText(List<ServiceModel> serviceModels)
    {
        var code = new StringBuilder();

        // 生成文件头
        GenerateFileHeader(code, serviceModels);

        // 生成命名空间和类开始
        GenerateClassHeader(code);

        // 生成消息类型映射
        GenerateMessageTypeMapping(code, serviceModels);

        // 生成处理器映射
        GenerateHandlerMapping(code, serviceModels);

        // 生成服务代理映射
        GenerateServiceProxyMapping(code, serviceModels);

        // 生成 Hub + Method 元数据映射
        GenerateHandlerMetadataMap(code, serviceModels);

        // 生成元数据访问辅助方法
        GenerateHandlerMetadataHelpers(code);

        // 生成注册方法
        GenerateRegistrationMethod(code, serviceModels);

        // 生成快速分发方法
        GenerateDispatchMethod(code, serviceModels);

        // 生成字节流直接分发方法
        GenerateDispatchFromBytesMethod(code, serviceModels);

        // 生成服务初始化方法
        GenerateServiceInitializationMethod(code, serviceModels);


        // 生成统计和监控方法
        GenerateStatisticsMethod(code, serviceModels);

        // 生成类结尾
        GenerateClassFooter(code);

        return code.ToString();
    }

