        code.AppendLine("    /// 服务代理映射 - 运行时初始化");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    private readonly Dictionary<string, IGeneratedServiceProxy> ServiceProxyMap = new(StringComparer.Ordinal);");
        code.AppendLine();

        // 生成服务实例字段 - 通过静态初始化方法设置
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 静态服务实例 - 由运行时初始化设置");
        code.AppendLine("    /// </summary>");
        foreach (var service in serviceModels)
        {
            var serviceInstanceField = $"_{service.InterfaceName.TrimStart('I').ToLower()}Instance";
            code.AppendLine($"    private {service.InterfaceFullName}? {serviceInstanceField};");
        }
        code.AppendLine();
    }

    /// <summary>
    /// 生成 Hub + Method 元数据映射，用于高性能反序列化
    /// </summary>
    private static void GenerateHandlerMetadataMap(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 编译时生成的 Hub + Method 元数据映射 - 支持零反射反序列化");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    private static readonly Dictionary<string, Dictionary<string, HandlerMetadata>> HandlerMetadataMap = new(StringComparer.Ordinal)");
        code.AppendLine("    {");

        foreach (var service in serviceModels)
        {
            var methodItems = service.Methods;
            var keys = new List<string> { service.InterfaceFullName };
            if (!string.IsNullOrEmpty(service.ServiceName) && !keys.Contains(service.ServiceName!))
            {
                keys.Add(service.ServiceName!);
            }

            foreach (var key in keys)
            {
                code.AppendLine($"        [\"{key}\"] = new Dictionary<string, HandlerMetadata>(StringComparer.Ordinal)");
                code.AppendLine("        {");

                foreach (var method in methodItems)
                {
                    var metadataInitializer = BuildHandlerMetadataInitializer(service, method, key);
                    code.Append(metadataInitializer);
                }

                code.AppendLine("        },");
            }
        }

        code.AppendLine("    };");
        code.AppendLine();
    }

    /// <summary>
    /// 生成元数据访问方法
    /// 注意：HandlerMetadata 类型定义在运行时 PulseRPC.Server.Engine 命名空间
    /// </summary>
    private static void GenerateHandlerMetadataHelpers(StringBuilder code)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 尝试获取指定 Hub 与方法的预编译元数据");
        code.AppendLine("    /// 实现 AbstractCompiledMessageDispatcher.TryGetHandlerMetadata");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public override bool TryGetHandlerMetadata(string serviceName, string methodName, [System.Diagnostics.CodeAnalysis.NotNullWhen(true)] out HandlerMetadata? metadata)");
        code.AppendLine("    {");
        code.AppendLine("        if (HandlerMetadataMap.TryGetValue(serviceName, out var methods) && methods.TryGetValue(methodName, out metadata))");
        code.AppendLine("        {");
        code.AppendLine("            return true;");
        code.AppendLine("        }");
        code.AppendLine("        metadata = null;");
        code.AppendLine("        return false;");
        code.AppendLine("    }");
        code.AppendLine();
    }

    private static string BuildHandlerMetadataInitializer(ServiceModel service, MethodModel method, string serviceKey)
    {
        var indent = "            ";
        var builder = new StringBuilder();

        // 使用运行时 HandlerMetadata 构造函数
        builder.Append($"{indent}[\"{method.MethodName}\"] = new HandlerMetadata(");
        builder.AppendLine();
        builder.AppendLine($"{indent}    serviceName: \"{service.InterfaceFullName}\",");
        builder.AppendLine($"{indent}    methodName: \"{method.MethodName}\",");
        builder.AppendLine($"{indent}    requestType: {GetRequestTypeExpression(method)},");
        builder.AppendLine($"{indent}    responseType: {GetResponseTypeExpression(method)},");
        builder.AppendLine($"{indent}    requestIsMemoryPackable: {GetRequestIsMemoryPackable(method)},");
        builder.AppendLine($"{indent}    responseIsMemoryPackable: {GetResponseIsMemoryPackable(method)},");
        builder.AppendLine($"{indent}    isOneWay: {IsOneWayMethod(method)},");
        builder.AppendLine($"{indent}    signature: \"{service.InterfaceFullName}.{method.MethodName}\"),");

        return builder.ToString();
    }

    private static string GetRequestTypeExpression(MethodModel method)
    {
        if (method.FirstParameter == null)
        {
            return "null";
        }

        return $"typeof({method.FirstParameter.TypeFullName})";
    }

    private static string GetResponseTypeExpression(MethodModel method)
    {
        if (string.IsNullOrEmpty(method.ReturnTypeFullName) || method.ReturnTypeFullName == "void" || method.ReturnTypeFullName == "System.Void")
        {
            return "null";
        }

        if (IsTaskTypeWithoutResult(method.ReturnTypeFullName))
        {
            return "null";
        }

        if (method.ReturnTypeFullName.StartsWith("System.Threading.Tasks.Task<", StringComparison.Ordinal))
        {
            var payloadType = ExtractGenericType(method.ReturnTypeFullName);
            return payloadType != null ? $"typeof({payloadType})" : "null";
        }

        if (method.ReturnTypeFullName.StartsWith("System.Threading.Tasks.ValueTask<", StringComparison.Ordinal))
        {
            var payloadType = ExtractGenericType(method.ReturnTypeFullName);
            return payloadType != null ? $"typeof({payloadType})" : "null";
        }

        return $"typeof({method.ReturnTypeFullName})";
    }

    private static string GetReturnTypeExpression(MethodModel method)
    {
        if (string.IsNullOrEmpty(method.ReturnTypeFullName) || method.ReturnTypeFullName == "void" || method.ReturnTypeFullName == "System.Void")
        {
            return "null";
        }

        return $"typeof({method.ReturnTypeFullName})";
    }

    private static string GetRequestTypeFullName(MethodModel method)
    {
        if (method.FirstParameter == null)
        {
            return "null";
        }

        return $"\"{method.FirstParameter.TypeFullName}\"";
    }

    private static string GetResponseTypeFullName(MethodModel method)
    {
        if (string.IsNullOrEmpty(method.ReturnTypeFullName) || method.ReturnTypeFullName == "void" || method.ReturnTypeFullName == "System.Void")
        {
            return "null";
        }

        var payloadType = GetResponsePayloadType(method.ReturnTypeFullName);
        return payloadType != null ? $"\"{payloadType}\"" : "null";
    }

    private static string GetReturnTypeFullName(MethodModel method)
    {
        if (string.IsNullOrEmpty(method.ReturnTypeFullName) || method.ReturnTypeFullName == "void" || method.ReturnTypeFullName == "System.Void")
        {
            return "null";
        }

        return $"\"{method.ReturnTypeFullName}\"";
    }

    private static string GetRequestIsMemoryPackable(MethodModel method)
    {
        return method.FirstParameter?.IsMemoryPackable == true ? "true" : "false";
    }

    private static string GetResponseIsMemoryPackable(MethodModel method)
    {
        var payloadType = GetResponsePayloadType(method.ReturnTypeFullName);
        if (payloadType == null)
        {
            return "false";
        }

        // TODO: 增强 MemoryPackable 检测（当前生成器尚未标记响应类型）
        return "false";
    }

    private static string IsValueTask(MethodModel method)
    {
        if (string.IsNullOrEmpty(method.ReturnTypeFullName))
        {
            return "false";
        }

        return method.ReturnTypeFullName.StartsWith("System.Threading.Tasks.ValueTask", StringComparison.Ordinal)
            ? "true"
            : "false";
    }

    private static bool IsTaskTypeWithoutResult(string? typeFullName)
    {
        if (string.IsNullOrEmpty(typeFullName))
        {
            return false;
        }

        return string.Equals(typeFullName, "System.Threading.Tasks.Task", StringComparison.Ordinal) ||
               string.Equals(typeFullName, "System.Threading.Tasks.ValueTask", StringComparison.Ordinal);
    }

    private static string? GetResponsePayloadType(string? returnTypeFullName)
    {
        if (string.IsNullOrEmpty(returnTypeFullName))
        {
            return null;
        }

        if (returnTypeFullName!.StartsWith("System.Threading.Tasks.Task<", StringComparison.Ordinal) ||
            returnTypeFullName.StartsWith("System.Threading.Tasks.ValueTask<", StringComparison.Ordinal))
        {
            return ExtractGenericType(returnTypeFullName);
        }

        if (returnTypeFullName == "void" || returnTypeFullName == "System.Void")
        {
            return null;
        }

        return returnTypeFullName;
    }

    private static string? ExtractGenericType(string typeFullName)
    {
        var start = typeFullName.IndexOf('<');
        var end = typeFullName.LastIndexOf('>');

        if (start < 0 || end <= start + 1)
        {
            return null;
        }

        return typeFullName.Substring(start + 1, end - start - 1);
    }

    private static string IsOneWayMethod(MethodModel method)
    {
        // 单向方法：返回类型为 void、Task 或 ValueTask（无结果）
        bool isOneWay = string.IsNullOrEmpty(method.ReturnTypeFullName) ||
                        method.ReturnTypeFullName == "void" ||
                        method.ReturnTypeFullName == "System.Void" ||
                        method.ReturnTypeFullName == "System.Threading.Tasks.Task" ||
                        method.ReturnTypeFullName == "System.Threading.Tasks.ValueTask";
        return isOneWay.ToString().ToLowerInvariant();
    }

    /// <summary>
    /// 生成处理器映射
    /// </summary>
    private static void GenerateHandlerMapping(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 预编译的消息处理器映射 - 直接方法调用，无反射开销");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    private readonly Dictionary<Type, MessageHandlerInfo> HandlerMap;");
        code.AppendLine();
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 构造函数 - 初始化处理器映射");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public CompiledMessageDispatcher()");
        code.AppendLine("    {");
        code.AppendLine("        HandlerMap = new Dictionary<Type, MessageHandlerInfo>");
        code.AppendLine("        {");

        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                if (method.IsSingleParameter)
                {
                    var handlerName = $"Handle{service.InterfaceName.TrimStart('I')}{method.MethodName}";
                    var isAsync = method.IsAsync;
                    var serviceName = service.InterfaceName;

                    code.AppendLine($"        [typeof({method.FirstParameter!.TypeFullName})] = new MessageHandlerInfo");
                    code.AppendLine("        {");
                    code.AppendLine($"            ServiceType = typeof({serviceName}),");
                    code.AppendLine($"            MethodName = \"{method.MethodName}\",");
                    code.AppendLine($"            IsAsync = {isAsync.ToString().ToLower()},");
                    code.AppendLine($"            Handler = (message, cancellationToken) => this.{handlerName}(message, cancellationToken),");
                    code.AppendLine($"            Priority = MessagePriority.{GetMessagePriority(method)},");
                    code.AppendLine($"            EstimatedLatencyMs = {GetEstimatedLatency(method)},");
                    code.AppendLine("        },");
                }
            }
        }

        code.AppendLine("        };");
        code.AppendLine("    }");
        code.AppendLine();
    }

    /// <summary>
    /// 生成注册方法 - 让运行时分发器可以注册编译时生成的处理器
    /// </summary>
    private static void GenerateRegistrationMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 将编译时生成的处理器注册到运行时分发器");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public override void RegisterHandlers(IStaticMessageDispatcher dispatcher)");
        code.AppendLine("    {");
        code.AppendLine("        if (dispatcher == null)");
        code.AppendLine("            throw new ArgumentNullException(nameof(dispatcher));");
        code.AppendLine();

        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                if (method.IsSingleParameter)
                {
                    var handlerName = $"Handle{service.InterfaceName.TrimStart('I')}{method.MethodName}";
                    var requestType = method.FirstParameter!.TypeFullName;

                    code.AppendLine($"        // 注册 {service.InterfaceName}.{method.MethodName}");
                    code.AppendLine($"        dispatcher.RegisterHandler<{requestType}>(this.{handlerName});");
                }
            }
        }

        code.AppendLine("    }");
        code.AppendLine();

    }

    /// <summary>
    /// 生成快速分发方法
    /// </summary>
    private static void GenerateDispatchMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 高性能消息分发 - 编译时优化的快速路径");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        code.AppendLine("    public override async ValueTask<object?> DispatchAsync(");
        code.AppendLine("        object message, ");
        code.AppendLine("        CancellationToken cancellationToken = default)");
        code.AppendLine("    {");
        code.AppendLine("        var messageType = message.GetType();");
        code.AppendLine("        ");
        code.AppendLine("        // 快速路径：预编译的类型检查和分发");
        code.AppendLine("        if (HandlerMap.TryGetValue(messageType, out var handlerInfo))");
        code.AppendLine("        {");
        code.AppendLine("            var startTime = Stopwatch.GetTimestamp();");
        code.AppendLine("            ");
        code.AppendLine("            try");
        code.AppendLine("            {");
        code.AppendLine("                var result = await handlerInfo.Handler(message, cancellationToken);");
        code.AppendLine("                ");
        code.AppendLine("                // 记录成功指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchSuccess(messageType.Name, elapsedMs);");
        code.AppendLine("                ");
        code.AppendLine("                return result;");
        code.AppendLine("            }");
        code.AppendLine("            catch (Exception ex)");
        code.AppendLine("            {");
        code.AppendLine("                // 记录错误指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchError(messageType.Name, elapsedMs, ex);");
        code.AppendLine("                throw;");
        code.AppendLine("            }");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        // 慢速路径：未识别的消息类型");
        code.AppendLine("        Metrics.RecordUnknownMessageType(messageType.Name);");
        code.AppendLine("        throw new InvalidOperationException($\"未找到消息类型 '{messageType.Name}' 的处理器\");");
        code.AppendLine("    }");
        code.AppendLine();
    }

    /// <summary>
    /// 生成字节流直接分发方法 - 零反射，高性能路径
    /// </summary>
    private static void GenerateDispatchFromBytesMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 直接从字节流分发到服务代理 - 无需反序列化为中间对象");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        code.AppendLine("    public override async ValueTask<object?> DispatchFromBytesAsync(");
        code.AppendLine("        string serviceName,");
        code.AppendLine("        string methodName,");
        code.AppendLine("        ReadOnlyMemory<byte> payloadData,");
        code.AppendLine("        CancellationToken cancellationToken = default)");
        code.AppendLine("    {");
        code.AppendLine("        // 快速路径：直接查找服务代理");
        code.AppendLine("        if (ServiceProxyMap.TryGetValue(serviceName, out var proxy))");
        code.AppendLine("        {");
        code.AppendLine("            var startTime = Stopwatch.GetTimestamp();");
        code.AppendLine("            ");
        code.AppendLine("            try");
        code.AppendLine("            {");
        code.AppendLine("                // 直接调用生成的代理方法");
        code.AppendLine("                var result = await proxy.InvokeAsync(methodName, payloadData, cancellationToken);");
        code.AppendLine("                ");
        code.AppendLine("                // 记录成功指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchSuccess($\"{serviceName}.{methodName}\", elapsedMs);");
        code.AppendLine("                ");
        code.AppendLine("                return result;");
        code.AppendLine("            }");
        code.AppendLine("            catch (Exception ex)");
        code.AppendLine("            {");
        code.AppendLine("                // 记录错误指标");
        code.AppendLine("                var elapsedMs = (Stopwatch.GetTimestamp() - startTime) * 1000.0 / Stopwatch.Frequency;");
        code.AppendLine("                Metrics.RecordDispatchError($\"{serviceName}.{methodName}\", elapsedMs, ex);");
        code.AppendLine("                throw;");
        code.AppendLine("            }");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        // 慢速路径：未找到服务");
        code.AppendLine("        Metrics.RecordUnknownMessageType($\"{serviceName}.{methodName}\");");
        code.AppendLine("        throw new ServiceNotFoundException($\"服务 '{serviceName}' 未找到或未注册\");");
        code.AppendLine("    }");
        code.AppendLine();
    }

    /// <summary>
    /// 生成服务初始化方法
    /// </summary>
    private static void GenerateServiceInitializationMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 初始化服务实例 - 由运行时调用");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public override void InitializeServices(IServiceProvider serviceProvider)");
        code.AppendLine("    {");
        code.AppendLine("        if (serviceProvider == null)");
        code.AppendLine("            throw new ArgumentNullException(nameof(serviceProvider));");
        code.AppendLine("        ");

        foreach (var service in serviceModels)
        {
            var serviceInstanceField = $"_{service.InterfaceName.TrimStart('I').ToLower()}Instance";
            code.AppendLine($"        {serviceInstanceField} = serviceProvider.GetRequiredService<{service.InterfaceFullName}>();");
        }

        code.AppendLine("        ");
        code.AppendLine("        // 注册服务代理");
        foreach (var service in serviceModels)
        {
            var serviceInstanceField = $"_{service.InterfaceName.TrimStart('I').ToLower()}Instance";
            var proxyClassName = $"{service.InterfaceName.TrimStart('I')}Proxy";
            code.AppendLine($"        ServiceProxyMap[\"{service.InterfaceFullName}\"] = new {service.Namespace}.Generated.{proxyClassName}({serviceInstanceField}!);");
        }

        code.AppendLine("        ");
        code.AppendLine("        // 标记为已初始化");
        code.AppendLine("        _isInitialized = true;");
        code.AppendLine("    }");
        code.AppendLine();
    }


    /// <summary>
    /// 生成具体的处理器方法
    /// </summary>
    private static void GenerateHandlerMethods(StringBuilder code, List<ServiceModel> serviceModels)
    {
        foreach (var service in serviceModels)
        {
            foreach (var method in service.Methods)
            {
                if (method.IsSingleParameter)
                {
                    var handlerName = $"Handle{service.InterfaceName.TrimStart('I')}{method.MethodName}";
                    var serviceName = service.InterfaceName;
                    var requestType = method.FirstParameter!.TypeFullName;
                    var responseType = method.ReturnTypeFullName ?? "object";
                    var isAsync = method.IsAsync;

                    code.AppendLine($"    /// <summary>");
                    code.AppendLine($"    /// 生成的处理器：{serviceName}.{method.MethodName}");
                    code.AppendLine($"    /// </summary>");
                    code.AppendLine($"    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
                    code.AppendLine($"    private async ValueTask<object?> {handlerName}(");
                    code.AppendLine($"        object message, ");
                    code.AppendLine($"        CancellationToken cancellationToken)");
                    code.AppendLine($"    {{");
                    code.AppendLine($"        var typedMessage = ({requestType})message;");
                    var serviceInstanceField = $"_{service.InterfaceName.TrimStart('I').ToLower()}Instance";
                    code.AppendLine($"        var service = {serviceInstanceField} ?? throw new InvalidOperationException(\"Service {serviceName} not initialized. Call InitializeServices first.\");");
                    code.AppendLine($"        ");

                    if (isAsync)
                    {
                        if (responseType == "void" || responseType == "System.Threading.Tasks.Task" || responseType == "System.Threading.Tasks.ValueTask")
                        {
                            code.AppendLine($"        await service.{method.MethodName}(typedMessage).ConfigureAwait(false);");
                            code.AppendLine($"        return null;");
                        }
                        else
                        {
                            code.AppendLine($"        var result = await service.{method.MethodName}(typedMessage).ConfigureAwait(false);");
                            code.AppendLine($"        return result;");
                        }
                    }
                    else
                    {
                        if (responseType == "void")
                        {
                            code.AppendLine($"        service.{method.MethodName}(typedMessage);");
                            code.AppendLine($"        return null;");
                        }
                        else
                        {
                            code.AppendLine($"        var result = service.{method.MethodName}(typedMessage);");
                            code.AppendLine($"        return result;");
                        }
                    }

                    code.AppendLine($"    }}");
                    code.AppendLine();
                }
            }
        }
    }

    /// <summary>
    /// 生成统计和监控方法
    /// </summary>
    private static void GenerateStatisticsMethod(StringBuilder code, List<ServiceModel> serviceModels)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 获取分发器统计信息");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public override object GetStatistics()");
        code.AppendLine("    {");
        code.AppendLine("        return new DispatcherStatistics");
        code.AppendLine("        {");
        code.AppendLine($"            TotalHandlers = {GetTotalHandlerCount(serviceModels)},");
        code.AppendLine($"            TotalServices = {serviceModels.Count},");
        code.AppendLine("            Metrics = Metrics.GetSnapshot(),");
        code.AppendLine($"            GeneratedAt = DateTime.UtcNow,");
        code.AppendLine("            HandlerMap = HandlerMap.ToDictionary(");
        code.AppendLine("                kvp => kvp.Key.Name, ");
        code.AppendLine("                kvp => kvp.Value)");
        code.AppendLine("        };");
        code.AppendLine("    }");
        code.AppendLine();

        // 生成 IsInitialized 属性
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 检查是否已正确初始化");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public override bool IsInitialized => _isInitialized;");
        code.AppendLine();
        code.AppendLine("    private bool _isInitialized;");
        code.AppendLine();

        // 生成性能指标类
        GenerateMetricsClass(code);

        // 生成统计数据类
        GenerateStatisticsDataClasses(code);

        // 生成具体的处理器方法
        GenerateHandlerMethods(code, serviceModels);
    }

    /// <summary>
    /// 生成性能指标类
    /// </summary>
    private static void GenerateMetricsClass(StringBuilder code)
    {
        code.AppendLine("    /// <summary>");
        code.AppendLine("    /// 分发器性能指标");
        code.AppendLine("    /// </summary>");
        code.AppendLine("    public static class Metrics");
        code.AppendLine("    {");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> SuccessCount = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> ErrorCount = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, double> TotalLatency = new();");
        code.AppendLine("        private static readonly ConcurrentDictionary<string, long> UnknownTypes = new();");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordDispatchSuccess(string messageType, double latencyMs)");
        code.AppendLine("        {");
        code.AppendLine("            SuccessCount.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("            TotalLatency.AddOrUpdate(messageType, latencyMs, (_, total) => total + latencyMs);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordDispatchError(string messageType, double latencyMs, Exception ex)");
        code.AppendLine("        {");
        code.AppendLine("            ErrorCount.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("            TotalLatency.AddOrUpdate(messageType, latencyMs, (_, total) => total + latencyMs);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static void RecordUnknownMessageType(string messageType)");
        code.AppendLine("        {");
        code.AppendLine("            UnknownTypes.AddOrUpdate(messageType, 1, (_, count) => count + 1);");
        code.AppendLine("        }");
        code.AppendLine("        ");
        code.AppendLine("        public static MetricsSnapshot GetSnapshot()");
        code.AppendLine("        {");
        code.AppendLine("            return new MetricsSnapshot");
        code.AppendLine("            {");
        code.AppendLine("                SuccessCount = new Dictionary<string, long>(SuccessCount),");
        code.AppendLine("                ErrorCount = new Dictionary<string, long>(ErrorCount),");
        code.AppendLine("                TotalLatency = new Dictionary<string, double>(TotalLatency),");
        code.AppendLine("                UnknownTypes = new Dictionary<string, long>(UnknownTypes)");
        code.AppendLine("            };");
        code.AppendLine("        }");
        code.AppendLine("    }");
        code.AppendLine();
    }

