        sb.AppendLine("        public async Task UnsubscribeAsync(string connectionId, string eventName)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (string.IsNullOrEmpty(connectionId))");
        sb.AppendLine("                throw new ArgumentException(\"Connection ID cannot be null or empty\", nameof(connectionId));");
