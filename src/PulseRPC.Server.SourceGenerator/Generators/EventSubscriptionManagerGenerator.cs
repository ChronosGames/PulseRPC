        sb.AppendLine("                    }");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            // 从连接订阅表中移除");
        sb.AppendLine("            if (_connectionSubscriptions.TryGetValue(connectionId, out var connectionSubscriptions))");
        sb.AppendLine("            {");
        sb.AppendLine("                connectionSubscriptions.Remove(eventName);");
        sb.AppendLine();
