    private static void GenerateCleanupMethods(StringBuilder sb)
    {
        sb.AppendLine("        /// <inheritdoc/>");
        sb.AppendLine("        public async Task CleanupDisconnectedSubscriptionsAsync()");
        sb.AppendLine("        {");
        sb.AppendLine("            ThrowIfDisposed();");
        sb.AppendLine();
        sb.AppendLine("            var cleanupTasks = new List<Task>();");
        sb.AppendLine("            var now = DateTime.UtcNow;");
        sb.AppendLine();
        sb.AppendLine("            // 清理超时的订阅（如果有配置超时时间）");
        sb.AppendLine("            foreach (var kvp in _eventSubscriptions.ToList())");
        sb.AppendLine("            {");
        sb.AppendLine("                var eventName = kvp.Key;");
        sb.AppendLine("                var subscriptions = kvp.Value;");
        sb.AppendLine();
        sb.AppendLine("                var expiredEntries = subscriptions.Where(entry =>");
        sb.AppendLine("                    entry.Options.TimeToLive.HasValue &&");
        sb.AppendLine("                    now - entry.SubscribeTime > entry.Options.TimeToLive.Value).ToList();");
        sb.AppendLine();
        sb.AppendLine("                foreach (var expiredEntry in expiredEntries)");
        sb.AppendLine("                {");
        sb.AppendLine("                    cleanupTasks.Add(UnsubscribeAsync(expiredEntry.ConnectionId, eventName));");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            if (cleanupTasks.Count > 0)");
        sb.AppendLine("            {");
        sb.AppendLine("                await Task.WhenAll(cleanupTasks);");
        sb.AppendLine("                _logger.LogInformation(\"Cleaned up {Count} expired subscriptions\", cleanupTasks.Count);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 辅助方法
        sb.AppendLine("        private static string GetSubscriptionKey(string connectionId, string eventName)");
        sb.AppendLine("        {");
        sb.AppendLine("            return $\"{connectionId}:{eventName}\";");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private static bool TagsMatch(Dictionary<string, string> entryTags, Dictionary<string, string> queryTags)");
        sb.AppendLine("        {");
        sb.AppendLine("            return queryTags.All(queryTag =>");
        sb.AppendLine("                entryTags.TryGetValue(queryTag.Key, out var value) && value == queryTag.Value);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private void ThrowIfDisposed()");
        sb.AppendLine("        {");
        sb.AppendLine("            if (_disposed)");
        sb.AppendLine("                throw new ObjectDisposedException(nameof(GeneratedEventSubscriptionManager));");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

