using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;
using PulseRPC.Server.SourceGenerator.Models;

namespace PulseRPC.Server.SourceGenerator.Generators;

/// <summary>
/// 服务代理生成器 - 生成高性能的服务调用代理
/// </summary>
public static class ServiceProxyGenerator
{
    /// <summary>
    /// 为服务模型生成代理类代码
    /// </summary>
    public static string GenerateServiceProxy(ServiceModel serviceModel)
    {
        var sb = new StringBuilder();

        // 文件头部
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// This code was generated by PulseRPC.Server.SourceGenerator");
        sb.AppendLine("// Changes to this file may cause incorrect behavior and will be lost if the code is regenerated.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine();

        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS8019 // Unnecessary using directive");
        sb.AppendLine();

        // Using statements
        GenerateUsingStatements(sb);

        // Namespace
        sb.AppendLine($"namespace {serviceModel.Namespace}.Generated;");
        sb.AppendLine();

        // 生成代理类
        GenerateProxyClass(sb, serviceModel);

        return sb.ToString();
    }

    /// <summary>
    /// 生成using语句
    /// </summary>
    private static void GenerateUsingStatements(StringBuilder sb)
    {
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Threading;");
        sb.AppendLine("using System.Threading.Tasks;");
        sb.AppendLine("using System.Runtime.CompilerServices;");
        sb.AppendLine("using MemoryPack;");
        sb.AppendLine("using PulseRPC.Server.Abstractions;");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成代理类
    /// </summary>
    private static void GenerateProxyClass(StringBuilder sb, ServiceModel serviceModel)
    {
        var proxyClassName = $"{serviceModel.InterfaceName.TrimStart('I')}Proxy";

        sb.AppendLine($"/// <summary>");
        sb.AppendLine($"/// Generated high-performance proxy for {serviceModel.InterfaceName}");
        sb.AppendLine($"/// Uses Protocol ID based routing - zero string allocation");
        sb.AppendLine($"/// </summary>");
        sb.AppendLine($"public sealed partial class {proxyClassName}");
        sb.AppendLine("{");

        // 服务实现字段
        GenerateFields(sb, serviceModel);

        // 构造函数
        GenerateConstructor(sb, serviceModel, proxyClassName);

        // 为每个方法生成专用调用方法（仅 Protocol ID 版本）
        GenerateMethodInvokers(sb, serviceModel);

        // 辅助方法
        GenerateHelperMethods(sb, serviceModel);

        sb.AppendLine("}");
    }

    /// <summary>
    /// 生成字段
    /// </summary>
    private static void GenerateFields(StringBuilder sb, ServiceModel serviceModel)
    {
        sb.AppendLine($"    private readonly {serviceModel.InterfaceFullName} _implementation;");
        sb.AppendLine($"    private static readonly string ServiceName = \"{serviceModel.InterfaceFullName}\";");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成构造函数
    /// </summary>
    private static void GenerateConstructor(StringBuilder sb, ServiceModel serviceModel, string proxyClassName)
    {
        sb.AppendLine($"    public {proxyClassName}({serviceModel.InterfaceFullName} implementation)");
        sb.AppendLine("    {");
        sb.AppendLine("        _implementation = implementation ?? throw new ArgumentNullException(nameof(implementation));");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// 为每个方法生成专用调用器（仅 Protocol ID 版本）
    /// </summary>
    private static void GenerateMethodInvokers(StringBuilder sb, ServiceModel serviceModel)
    {
        foreach (var method in serviceModel.Methods)
        {
            var parameterVariableNames = GetSafeParameterVariableNames(method);

            // 生成基于协议号的调用器（零字符串分配）
            GenerateProtocolIdMethodInvoker(sb, method, serviceModel.InterfaceName, parameterVariableNames);
        }
    }

    /// <summary>
    /// 生成单个方法的协议号调用器（用于路由表直接调用）
    /// </summary>
    private static void GenerateProtocolIdMethodInvoker(StringBuilder sb, MethodModel method, string serviceName, IReadOnlyList<string> parameterVariableNames)
    {
        sb.AppendLine($"    /// <summary>");
        sb.AppendLine($"    /// Protocol ID invoker for {method.MethodName} (0x{method.ProtocolId:X4})");
        sb.AppendLine($"    /// Called directly by ServiceRoutingTable for maximum performance");
        sb.AppendLine($"    /// </summary>");
        sb.AppendLine("    [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine($"    public async ValueTask<object?> Invoke_{method.MethodName}_Async(ReadOnlyMemory<byte> data, CancellationToken cancellationToken)");
        sb.AppendLine("    {");

        // 反序列化参数
        GenerateParameterDeserialization(sb, method, parameterVariableNames);

        // 调用服务方法
        GenerateServiceMethodCall(sb, method, parameterVariableNames);

        sb.AppendLine("    }");
        sb.AppendLine();
    }


    /// <summary>
    /// 生成参数反序列化代码
    /// </summary>
    private static void GenerateParameterDeserialization(StringBuilder sb, MethodModel method, IReadOnlyList<string> parameterVariableNames)
    {
        if (!method.HasParameters)
        {
            sb.AppendLine("        // Method has no parameters");
            return;
        }

        if (method.IsSingleParameter)
        {
            var param = method.FirstParameter!;
            var variableName = parameterVariableNames[0];
            if (!param.IsMemoryPackable)
            {
                sb.AppendLine($"        // Warning: Parameter type {param.TypeName} is not MemoryPackable");
            }

            sb.AppendLine($"        // Deserialize single parameter: {param.TypeName}");
            sb.AppendLine($"        var {variableName} = MemoryPackSerializer.Deserialize<{param.TypeFullName}>(data.Span)!;");
        }
        else
        {
            // 多参数处理（需要参数包装类）
            sb.AppendLine("        // Multi-parameter deserialization");
            sb.AppendLine("        var parameters = DeserializeMultipleParameters(data);");

            for (int i = 0; i < method.Parameters.Count; i++)
            {
                var param = method.Parameters[i];
                var variableName = parameterVariableNames[i];
                sb.AppendLine($"        var {variableName} = ({param.TypeFullName})parameters[{i}];");
            }
        }

        sb.AppendLine();
    }

    /// <summary>
    /// 生成服务方法调用代码
    /// </summary>
    private static void GenerateServiceMethodCall(StringBuilder sb, MethodModel method, IReadOnlyList<string> parameterVariableNames)
    {
        var paramList = method.HasParameters ?
            string.Join(", ", parameterVariableNames) :
            "";

        if (method.IsAsync)
        {
            if (method.IsGenericTask)
            {
                // ValueTask<T> 或 Task<T>
                sb.AppendLine($"        var result = await _implementation.{method.MethodName}({paramList});");
                sb.AppendLine("        return result;");
            }
            else
            {
                // ValueTask 或 Task (void)
                sb.AppendLine($"        await _implementation.{method.MethodName}({paramList});");
                sb.AppendLine("        return null;");
            }
        }
        else
        {
            // 同步方法
            if (method.ReturnTypeName == "void")
            {
                sb.AppendLine($"        _implementation.{method.MethodName}({paramList});");
                sb.AppendLine("        return null;");
            }
            else
            {
                sb.AppendLine($"        var result = _implementation.{method.MethodName}({paramList});");
                sb.AppendLine("        return result;");
            }
        }
    }

    /// <summary>
    /// 生成辅助方法
    /// </summary>
    private static IReadOnlyList<string> GetSafeParameterVariableNames(MethodModel method)
    {
        if (!method.HasParameters)
        {
            return Array.Empty<string>();
        }

        var names = new List<string>(method.Parameters.Count);

        foreach (var parameter in method.Parameters)
        {
            var rawName = parameter.Name;

            if (string.Equals(rawName, "cancellationToken", StringComparison.Ordinal))
            {
                names.Add("_" + rawName);
                continue;
            }

            // 避免与 C# 关键字冲突
            if (IsCSharpKeyword(rawName))
            {
                names.Add(rawName + "_");
                continue;
            }

            names.Add(rawName);
        }

        return names;
    }

    private static bool IsCSharpKeyword(string identifier)
    {
        return identifier switch
        {
            "abstract" or "as" or "base" or "bool" or "break" or "byte" or "case" or "catch" or "char" or "checked" or
            "class" or "const" or "continue" or "decimal" or "default" or "delegate" or "do" or "double" or "else" or
            "enum" or "event" or "explicit" or "extern" or "false" or "finally" or "fixed" or "float" or "for" or
            "foreach" or "goto" or "if" or "implicit" or "in" or "int" or "interface" or "internal" or "is" or
            "lock" or "long" or "namespace" or "new" or "null" or "object" or "operator" or "out" or "override" or
            "params" or "private" or "protected" or "public" or "readonly" or "ref" or "return" or "sbyte" or
            "sealed" or "short" or "sizeof" or "stackalloc" or "static" or "string" or "struct" or "switch" or
            "this" or "throw" or "true" or "try" or "typeof" or "uint" or "ulong" or "unchecked" or "unsafe" or
            "ushort" or "using" or "virtual" or "void" or "volatile" or "while"
                => true,
            _ => false
        };
    }

    private static void GenerateHelperMethods(StringBuilder sb, ServiceModel serviceModel)
    {
        // 多参数反序列化辅助方法
        GenerateMultiParameterDeserializer(sb, serviceModel);

        // 服务信息属性
        GenerateServiceInfo(sb, serviceModel);
    }

    /// <summary>
    /// 生成多参数反序列化方法
    /// </summary>
    private static void GenerateMultiParameterDeserializer(StringBuilder sb, ServiceModel serviceModel)
    {
        var hasMultiParamMethods = serviceModel.Methods.Any(m => m.Parameters.Count > 1);
        if (!hasMultiParamMethods) return;

        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Deserialize multiple parameters (fallback for complex scenarios)");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    private static object[] DeserializeMultipleParameters(ReadOnlyMemory<byte> data)");
        sb.AppendLine("    {");
        sb.AppendLine("        // TODO: Implement multi-parameter deserialization");
        sb.AppendLine("        // This requires a parameter wrapper class or tuple serialization");
        sb.AppendLine("        throw new NotImplementedException(\"Multi-parameter deserialization not yet implemented\");");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    /// <summary>
    /// 生成服务信息
    /// </summary>
    private static void GenerateServiceInfo(StringBuilder sb, ServiceModel serviceModel)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Service metadata for debugging and introspection");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    public static class ServiceInfo");
        sb.AppendLine("    {");
        sb.AppendLine($"        public const string Name = \"{serviceModel.InterfaceFullName}\";");
        sb.AppendLine($"        public const string ShortName = \"{serviceModel.InterfaceName}\";");
        sb.AppendLine($"        public const string Channel = \"{serviceModel.ChannelName}\";");
        sb.AppendLine($"        public const int MethodCount = {serviceModel.Methods.Count};");

        sb.AppendLine("        public static readonly string[] Methods = new[]");
        sb.AppendLine("        {");
        foreach (var method in serviceModel.Methods)
        {
            sb.AppendLine($"            \"{method.MethodName}\",");
        }
        sb.AppendLine("        };");

        sb.AppendLine("    }");
    }

    /// <summary>
    /// 生成SourceText用于编译器集成
    /// </summary>
    public static SourceText GenerateSourceText(ServiceModel serviceModel)
    {
        var code = GenerateServiceProxy(serviceModel);
        return SourceText.From(code, Encoding.UTF8);
    }
}
