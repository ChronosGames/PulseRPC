    private static void GenerateBatchEventProcessor(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 批量事件处理器 - 优化批量事件处理性能");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class BatchEventProcessor : IDisposable");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly PerformanceMetricsCollector _metrics;");
        sb.AppendLine("        private readonly SemaphoreSlim _processingLock = new(1, 1);");
        sb.AppendLine();

        sb.AppendLine("        public BatchEventProcessor(PerformanceMetricsCollector metrics)");
        sb.AppendLine("        {");
        sb.AppendLine("            _metrics = metrics;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public async Task ProcessBatchAsync<T>(IList<(string EventName, T EventData)> events)");
        sb.AppendLine("        {");
        sb.AppendLine("            await _processingLock.WaitAsync();");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                // 分组处理相同类型的事件");
        sb.AppendLine("                var groupedEvents = events.GroupBy(e => e.EventName);");
        sb.AppendLine("                ");
        sb.AppendLine("                var tasks = groupedEvents.Select(ProcessEventGroupAsync);");
        sb.AppendLine("                await Task.WhenAll(tasks);");
        sb.AppendLine("            }");
        sb.AppendLine("            finally");
        sb.AppendLine("            {");
        sb.AppendLine("                _processingLock.Release();");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private static async Task ProcessEventGroupAsync<T>(IGrouping<string, (string EventName, T EventData)> group)");
        sb.AppendLine("        {");
        sb.AppendLine("            // 这里可以进一步优化相同事件类型的批量处理");
        sb.AppendLine("            await Task.Delay(1); // 占位符");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void Dispose()");
        sb.AppendLine("        {");
        sb.AppendLine("            _processingLock?.Dispose();");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

    private static void GenerateMemoryPooledSerializer(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 内存池化序列化器 - 减少GC压力");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class PooledEventSerializer : IDisposable");
        sb.AppendLine("    {");
        sb.AppendLine("        private readonly ArrayPool<byte> _arrayPool = ArrayPool<byte>.Shared;");
        sb.AppendLine("        private readonly ConcurrentQueue<byte[]> _bufferCache = new();");
        sb.AppendLine();

        sb.AppendLine("        [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        public ReadOnlyMemory<byte> SerializeFast<T>(T data, Type? dataType = null)");
        sb.AppendLine("        {");
        sb.AppendLine("            // 获取缓存的缓冲区");
        sb.AppendLine("            var buffer = GetBuffer();");
        sb.AppendLine("            ");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                // 这里应该使用实际的序列化器（如MemoryPack）");
        sb.AppendLine("                // 目前返回空数据作为占位符");
        sb.AppendLine("                return ReadOnlyMemory<byte>.Empty;");
        sb.AppendLine("            }");
        sb.AppendLine("            finally");
        sb.AppendLine("            {");
        sb.AppendLine("                ReturnBuffer(buffer);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private byte[] GetBuffer()");
        sb.AppendLine("        {");
        sb.AppendLine("            return _bufferCache.TryDequeue(out var buffer) ? buffer : _arrayPool.Rent(4096);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private void ReturnBuffer(byte[] buffer)");
        sb.AppendLine("        {");
        sb.AppendLine("            if (_bufferCache.Count < 100) // 限制缓存大小");
        sb.AppendLine("            {");
        sb.AppendLine("                _bufferCache.Enqueue(buffer);");
        sb.AppendLine("            }");
        sb.AppendLine("            else");
        sb.AppendLine("            {");
        sb.AppendLine("                _arrayPool.Return(buffer);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void Dispose()");
        sb.AppendLine("        {");
        sb.AppendLine("            // 归还所有缓存的缓冲区");
        sb.AppendLine("            while (_bufferCache.TryDequeue(out var buffer))");
        sb.AppendLine("            {");
        sb.AppendLine("                _arrayPool.Return(buffer);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();
    }

