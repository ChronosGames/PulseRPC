    private static void GenerateHotPathMethods(StringBuilder sb)
    {
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 热路径事件发布 - 极致性能优化");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        private async ValueTask PublishHotPathEventAsync<T>(string eventName, T eventData)");
        sb.AppendLine("        {");
        sb.AppendLine("            // 使用无锁队列进行预处理");
        sb.AppendLine("            var pendingEvent = new PendingEvent(eventName, eventData!, typeof(T));");
        sb.AppendLine("            _hotPathQueue.Enqueue(pendingEvent);");
        sb.AppendLine("            ");
        sb.AppendLine("            // 如果队列过长，立即处理");
        sb.AppendLine("            if (_hotPathQueue.Count > 100)");
        sb.AppendLine("            {");
        sb.AppendLine("                ProcessHotPathEvents(null);");
        sb.AppendLine("            }");
        sb.AppendLine("            ");
        sb.AppendLine("            await ValueTask.CompletedTask;");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private void ProcessHotPathEvents(object? state)");
        sb.AppendLine("        {");
        sb.AppendLine("            var events = new List<PendingEvent>();");
        sb.AppendLine("            ");
        sb.AppendLine("            // 批量出队");
        sb.AppendLine("            while (events.Count < 50 && _hotPathQueue.TryDequeue(out var evt))");
        sb.AppendLine("            {");
        sb.AppendLine("                events.Add(evt);");
        sb.AppendLine("            }");
        sb.AppendLine("            ");
        sb.AppendLine("            if (events.Count == 0) return;");
        sb.AppendLine("            ");
        sb.AppendLine("            // 异步处理，避免阻塞定时器");
        sb.AppendLine("            _ = Task.Run(async () =>");
        sb.AppendLine("            {");
        sb.AppendLine("                try");
        sb.AppendLine("                {");
        sb.AppendLine("                    await ProcessHotPathEventsBatchAsync(events);");
        sb.AppendLine("                }");
        sb.AppendLine("                catch (Exception ex)");
        sb.AppendLine("                {");
        sb.AppendLine("                    _logger.LogError(ex, \"Error processing hot path events batch\");");
        sb.AppendLine("                }");
        sb.AppendLine("            });");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private async Task ProcessHotPathEventsBatchAsync(List<PendingEvent> events)");
        sb.AppendLine("        {");
        sb.AppendLine("            // 按事件名分组");
        sb.AppendLine("            var groupedEvents = events.GroupBy(e => e.EventName);");
        sb.AppendLine("            ");
        sb.AppendLine("            var tasks = groupedEvents.Select(async group =>");
        sb.AppendLine("            {");
        sb.AppendLine("                var eventName = group.Key;");
        sb.AppendLine("                var subscribers = _subscriptionManager.GetSubscribers(eventName);");
        sb.AppendLine("                ");
        sb.AppendLine("                if (!subscribers.Any()) return;");
        sb.AppendLine("                ");
        sb.AppendLine("                var channels = _channelManager.GetAuthenticatedChannels()");
        sb.AppendLine("                    .Where(c => subscribers.Contains(c.ConnectionId))");
        sb.AppendLine("                    .ToList();");
        sb.AppendLine("                ");
        sb.AppendLine("                // 批量序列化和发送");
        sb.AppendLine("                foreach (var evt in group)");
        sb.AppendLine("                {");
        sb.AppendLine("                    var serializedData = _serializer.SerializeFast(evt.EventData, evt.EventType);");
        sb.AppendLine("                    await SendToChannelsZeroCopyAsync(channels, serializedData);");
        sb.AppendLine("                }");
        sb.AppendLine("            });");
        sb.AppendLine("            ");
        sb.AppendLine("            await Task.WhenAll(tasks);");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

    private static void GeneratePerformanceMonitoringMethods(StringBuilder sb)
