    private static void GeneratePerformanceMetrics(StringBuilder sb)
    {
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// 性能指标收集器");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal sealed class PerformanceMetricsCollector");
        sb.AppendLine("    {");
        sb.AppendLine("        private long _eventsPublished;");
        sb.AppendLine("        private long _batchesProcessed;");
        sb.AppendLine("        private long _errors;");
        sb.AppendLine("        private readonly ConcurrentDictionary<string, PerformanceMetric> _eventMetrics = new();");
        sb.AppendLine();

        sb.AppendLine("        public IDisposable StartEventPublishing(string eventName)");
        sb.AppendLine("        {");
        sb.AppendLine("            return new EventPublishingActivity(eventName, this);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public IDisposable StartBatchPublishing(int eventCount)");
        sb.AppendLine("        {");
        sb.AppendLine("            return new BatchPublishingActivity(eventCount, this);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordEventPublished(string eventName, int subscriberCount)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _eventsPublished);");
        sb.AppendLine("            ");
        sb.AppendLine("            var metric = _eventMetrics.GetOrAdd(eventName, _ => new PerformanceMetric());");
        sb.AppendLine("            Interlocked.Increment(ref metric.Count);");
        sb.AppendLine("            Interlocked.Add(ref metric.TotalSubscribers, subscriberCount);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordEventError(string eventName, Exception exception)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _errors);");
        sb.AppendLine("            ");
        sb.AppendLine("            var metric = _eventMetrics.GetOrAdd(eventName, _ => new PerformanceMetric());");
        sb.AppendLine("            Interlocked.Increment(ref metric.Errors);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordBatchPublished(int eventCount)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _batchesProcessed);");
        sb.AppendLine("            Interlocked.Add(ref _eventsPublished, eventCount);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public void RecordBatchError(Exception exception)");
        sb.AppendLine("        {");
        sb.AppendLine("            Interlocked.Increment(ref _errors);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        public PerformanceMetrics GetSnapshot()");
        sb.AppendLine("        {");
        sb.AppendLine("            return new PerformanceMetrics");
        sb.AppendLine("            {");
        sb.AppendLine("                TotalEventsPublished = _eventsPublished,");
        sb.AppendLine("                TotalBatchesProcessed = _batchesProcessed,");
        sb.AppendLine("                TotalErrors = _errors,");
        sb.AppendLine("                EventMetrics = new Dictionary<string, PerformanceMetric>(_eventMetrics)");
        sb.AppendLine("            };");
        sb.AppendLine("        }");
        sb.AppendLine();

        // 生成内部活动类
        sb.AppendLine("        private sealed class EventPublishingActivity : IDisposable");
        sb.AppendLine("        {");
        sb.AppendLine("            private readonly string _eventName;");
        sb.AppendLine("            private readonly PerformanceMetricsCollector _collector;");
        sb.AppendLine("            private readonly System.Diagnostics.Stopwatch _stopwatch;");
        sb.AppendLine();

        sb.AppendLine("            public EventPublishingActivity(string eventName, PerformanceMetricsCollector collector)");
        sb.AppendLine("            {");
        sb.AppendLine("                _eventName = eventName;");
        sb.AppendLine("                _collector = collector;");
        sb.AppendLine("                _stopwatch = System.Diagnostics.Stopwatch.StartNew();");
        sb.AppendLine("            }");
        sb.AppendLine();

        sb.AppendLine("            public void Dispose()");
        sb.AppendLine("            {");
        sb.AppendLine("                _stopwatch.Stop();");
