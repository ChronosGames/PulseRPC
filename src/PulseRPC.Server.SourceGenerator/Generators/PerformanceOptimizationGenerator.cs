        sb.AppendLine("        }");
        sb.AppendLine();

        // 辅助方法
        sb.AppendLine("        [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        private static bool IsHotPathEvent(string eventName)");
        sb.AppendLine("        {");
        sb.AppendLine("            // 高频事件模式匹配");
        sb.AppendLine("            return eventName.Contains(\"Realtime\") || ");
        sb.AppendLine("                   eventName.Contains(\"Position\") || ");
        sb.AppendLine("                   eventName.Contains(\"Heartbeat\") ||");
        sb.AppendLine("                   eventName.EndsWith(\"Update\");");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private static EventRoutingContext CreateRoutingContext()");
        sb.AppendLine("        {");
        sb.AppendLine("            return new EventRoutingContext");
        sb.AppendLine("            {");
        sb.AppendLine("                Timestamp = DateTime.UtcNow");
        sb.AppendLine("            };");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        private static async ValueTask SendToChannelsZeroCopyAsync(");
        sb.AppendLine("            IEnumerable<IServerChannel> channels, ReadOnlyMemory<byte> data)");
        sb.AppendLine("        {");
        sb.AppendLine("            var tasks = channels.Select(async channel =>");
        sb.AppendLine("            {");
        sb.AppendLine("                try");
        sb.AppendLine("                {");
        sb.AppendLine("                    await channel.SendAsync(data, CancellationToken.None);");
        sb.AppendLine("                }");
        sb.AppendLine("                catch");
        sb.AppendLine("                {");
        sb.AppendLine("                    // 忽略发送失败，避免影响其他通道");
        sb.AppendLine("                }");
        sb.AppendLine("            });");
        sb.AppendLine("            ");
        sb.AppendLine("            await Task.WhenAll(tasks);");
        sb.AppendLine("        }");
        sb.AppendLine();

        sb.AppendLine("        private void ThrowIfDisposed()");
        sb.AppendLine("        {");
        sb.AppendLine("            if (_disposed)");
        sb.AppendLine("                throw new ObjectDisposedException(nameof(HighPerformanceEventPublisher));");
        sb.AppendLine("        }");
        sb.AppendLine();
    }

