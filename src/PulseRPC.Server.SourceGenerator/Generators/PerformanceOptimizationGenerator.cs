        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// 高性能事件发布 - 零拷贝、智能路由");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        [MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine("        public async Task PublishEventAsync<T>(string eventName, T eventData)");
        sb.AppendLine("        {");
        sb.AppendLine("            ThrowIfDisposed();");
        sb.AppendLine("            ");
        sb.AppendLine("            using var activity = _metrics.StartEventPublishing(eventName);");
        sb.AppendLine("            ");
        sb.AppendLine("            try");
        sb.AppendLine("            {");
        sb.AppendLine("                // 对于高频事件，使用热路径优化");
